def process(path: Path) -> bool:
    if not re.match(r"ch0(3[1-9]|[4-5][0-9]|60)", path.name):
        return False
    text = path.read_text(encoding="utf-8")
    orig = text
    for old, new in GLOBAL + CH31_60:
        text = text.replace(old, new)
    if text != orig:
        path.write_text(text, encoding="utf-8")
        return True
    return False

def main():
    changed = []
    for p in sorted(PROSE.glob("ch*.md")):
        if process(p):
            changed.append(p.name)
    # 顺带修 ch002/ch004 残留
    for name in ["ch002-师尊赠丹.md", "ch004-杖中有灵.md"]:
        p = PROSE / name
        t = p.read_text(encoding="utf-8")
        o = t
        for old, new in GLOBAL:
            t = t.replace(old, new)
        if t != o:
            p.write_text(t, encoding="utf-8")
            changed.append(name)
    print(f"Updated {len(changed)} files")
    for c in changed:
        print(f"  {c}")

if __name__ == "__main__":
    main()
