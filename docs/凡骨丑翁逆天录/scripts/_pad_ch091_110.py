def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def pad_chapter(n, path):
    raw = open(path, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    if hz(body) >= TARGET_LO:
        return
    if n not in PAD:
        return
    # insert before last paragraph (hook)
    parts = body.rsplit("\n\n", 1)
    if len(parts) == 2:
        new_body = parts[0] + "\n\n" + PAD[n].strip() + "\n\n" + parts[1]
    else:
        new_body = body + "\n\n" + PAD[n].strip()
    # rebuild
    title = raw.split("\n\n")[0]
    full = title + "\n\n" + (new_body.split("\n\n", 1)[1] if new_body.startswith("#") else new_body)
    if new_body.startswith("#"):
        full = new_body + "\n\n" + footer.lstrip("\n")
    else:
        full = title + "\n\n" + new_body.split("\n\n", 1)[-1 if not new_body.startswith("#") else 0]
    # simpler rebuild
    if body.startswith("#"):
        main = body.split("\n\n", 1)[1]
    else:
        main = body
    parts2 = main.rsplit("\n\n", 1)
    main = parts2[0] + "\n\n" + PAD[n].strip() + "\n\n" + parts2[1]
    full = title + "\n\n" + main + "\n\n" + footer.lstrip("\n")
    with open(path, "w", encoding="utf-8") as f:
        f.write(full.strip() + "\n")


def main():
    for n in range(91, 111):
        matches = glob.glob(os.path.join(PROSE, f"ch{n:03d}-*.md"))
        if not matches:
            print("MISSING", n)
            continue
        pad_chapter(n, matches[0])
    # report
    files = sorted(glob.glob(os.path.join(PROSE, "ch09[1-9]-*.md")) + glob.glob(os.path.join(PROSE, "ch100-*.md")) + glob.glob(os.path.join(PROSE, "ch10[1-9]-*.md")) + glob.glob(os.path.join(PROSE, "ch110-*.md")))
    print("FILE\tCHARS\tDUP\tOK")
    bad = []
    for p in files:
        raw = open(p, encoding="utf-8").read()
        b, _ = extract_body_and_footer(raw)
        h, d = hz(b), dup(b)
        ok = TARGET_LO <= h <= TARGET_HI and d < 0.02
        print(f"{os.path.basename(p)}\t{h}\t{round(d,4)}\t{ok}")
        if not ok:
            bad.append((os.path.basename(p), h, round(d, 4)))
    if bad:
        print("NEEDS_FIX", bad)
        sys.exit(1)
    print("ALL_OK", len(files))

if __name__ == "__main__":
    main()
