def count_body(text):
    m = re.search(r'^#.*?\n\n(.*?)\n\n---\n\n\*（上架', text, re.S)
    body = m.group(1) if m else text
    return len(re.findall(r'[\u4e00-\u9fff]', body))

def make_ch(title, body, footer):
    return f"# {title}\n\n{body.strip()}\n\n---\n\n{footer}\n"

def load_chapters(script_name):
    path = os.path.join(base, script_name)
    spec = importlib.util.spec_from_file_location(script_name, path)
    mod = importlib.util.module_from_spec(spec)
    import builtins
    real_open = builtins.open
    def patched_open(file, mode='r', *args, **kwargs):
        if 'w' in mode:
            return StringIO()
        return real_open(file, mode, *args, **kwargs)
    builtins.open = patched_open
    import sys
    from io import StringIO as _SIO
    old_stdout = sys.stdout
    sys.stdout = _SIO()
    try:
        spec.loader.exec_module(mod)
    finally:
        sys.stdout = old_stdout
        builtins.open = real_open
    return mod.CHAPTERS

def parse_ch(content):
    title_m = re.search(r'^# (.+)', content)
    footer_m = re.search(r'\*（上架[^\n]*）\*', content)
    body_m = re.search(r'^#.*?\n\n(.*?)\n\n---\n\n\*（上架', content, re.S)
    return title_m.group(1), body_m.group(1).strip(), footer_m.group(0)

def merge_extra(content, extra):
    title, body, footer = parse_ch(content)
    return make_ch(title, body + '\n\n' + extra.strip(), footer)

