"""Strip generic topup/corruption; add unique chapter-specific expansions."""
import re, os, glob, subprocess

BASE = os.path.dirname(os.path.abspath(__file__))
BIG = open(os.path.join(BASE, '_topup_big.txt'), encoding='utf-8').read().strip()
CORRUPT = re.compile(r'更鼓将尽，天边白一线。白里，廊下鼻下静半刻，粟壳香淡。')

def cjk_count(text):
    return len(re.findall(r'[\u4e00-\u9fff\u3400-\u4dbf]', text))

def strip_body(body):
    body = body.replace(BIG, '')
    body = CORRUPT.sub('', body)
    # collapse duplicate blank lines
    body = re.sub(r'\n{3,}', '\n\n', body)
    return body.strip()

def get_footer(text):
    parts = text.split('---')
    return '---'.join(parts[1:]).strip()

def write_chapter(path, title, body, footer):
    with open(path, 'w', encoding='utf-8') as f:
        f.write(f"# {title}\n\n{body.strip()}\n\n---\n\n{footer.strip()}\n")

# restore clean bases from batch scripts
for script in ['_fix_remaining.py', '_expand_batch3.py', '_expand_batch4.py', '_expand_batch5.py']:
    p = os.path.join(BASE, script)
    if os.path.exists(p):
        subprocess.run(['python', p], check=True, cwd=BASE)

