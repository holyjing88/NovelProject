def dedupe_paragraphs(text: str) -> str:
    """Remove duplicate paragraphs while preserving order."""
    lines = text.split('\n')
    # group into paragraphs (blank-line separated)
    paras = []
    buf = []
    for line in lines:
        if line.strip() == '':
            if buf:
                paras.append('\n'.join(buf))
                buf = []
            paras.append('')
        else:
            buf.append(line)
    if buf:
        paras.append('\n'.join(buf))

    seen = set()
    out = []
    for p in paras:
        if p == '':
            out.append('')
            continue
        key = re.sub(r'\s', '', p)
        if len(key) < 20:
            out.append(p)
            continue
        if key in seen:
            continue
        seen.add(key)
        out.append(p)

    return '\n'.join(out)


