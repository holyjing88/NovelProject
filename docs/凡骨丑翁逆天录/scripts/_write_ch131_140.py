# Remaining bodies written in continuation - file will be completed via StrReplace

def sentence_keys(text: str):
    keys = set()
    for sent in re.split(r"(?<=[。！？])", text):
        s = sent.strip()
        if len(s) < 8:
            continue
        keys.add(re.sub(r"\s+", "", s))
    return keys


def filter_new_block(block: str, seen: set) -> str:
    out = []
    for full in re.split(r"(?<=[。！？])", block):
        s = full.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(s) >= 8 and k in seen:
            continue
        seen.add(k)
        out.append(full)
    return "".join(out).strip()


def merge_expansions(body: str, n: int) -> str:
    seen = sentence_keys(body)
    for block in (
        MIDDLE.get(n, ""),
        MIDDLE2.get(n, ""),
        MEGA.get(n, ""),
        TOPUP.get(n, ""),
        ULTRA.get(n, ""),
        TOPUP2.get(n, ""),
    ):
        block = block.strip()
        if not block:
            continue
        add = filter_new_block(block, seen)
        if add:
            body = body + "\n\n" + add if body else add
    return body


def footer(n):
    ref, st = FOOTERS[n]
    return f"\n---\n\n章末\n\n（对照 `16` {ref} · **仙凡笔锋 v2**）\n\n**状态**：{st}\n"


