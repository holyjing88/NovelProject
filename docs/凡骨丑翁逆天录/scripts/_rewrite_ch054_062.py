FORBIDDEN = ["手稳恩不断", "泥岗根在", "叶汤远影"]


def dedupe_paras(body: str) -> str:
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for p in paras:
        key = re.sub(r"\s+", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


def pad_body(fname: str, body: str) -> str:
    body = dedupe_paras(body)
    all_blocks = (
        EXTEND.get(fname, [])
        + EXTRA2.get(fname, [])
        + EXTRA3.get(fname, [])
        + EXTRA4.get(fname, [])
    )
    for block in all_blocks:
        if hz(body) >= TARGET_LO:
            break
        body = body.rstrip() + "\n\n" + block
    body = dedupe_paras(body)
    if hz(body) < TARGET_LO and fname in FINAL_PAD:
        body = body.rstrip() + "\n\n" + FINAL_PAD[fname]
    return dedupe_paras(body)


def trim_body(body: str) -> str:
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    while hz("\n\n".join(paras)) > TARGET_HI and len(paras) > 5:
        paras.pop(-2 if paras[-1].startswith("---") else -1)
    return "\n\n".join(paras)


def write_chapter(fname: str, spec: dict) -> tuple[int, list[str]]:
    body = pad_body(fname, spec["body"].strip())
    if hz(body) > TARGET_HI:
        body = trim_body(body)
    issues: list[str] = []
    h = hz(body)
    if h < TARGET_LO:
        issues.append(f"SHORT {h}")
    elif h > TARGET_HI:
        issues.append(f"LONG {h}")
    for phrase in FORBIDDEN:
        if body.count(phrase) > 1:
            issues.append(f"DUP_PHRASE {phrase} x{body.count(phrase)}")
        elif body.count(phrase) == 1 and sum(
            1 for f, s in CHAPTERS.items() if phrase in s["body"]
        ) > 1:
            pass  # checked globally below
    footer = FOOTER.format(ref=spec["ref"], status=spec["status"])
    full = f"{spec['title']}\n\n{body}\n\n{footer}\n"
    (PROSE / fname).write_text(full, encoding="utf-8")
    return h, issues


def main() -> None:
    global_forbidden: dict[str, int] = {p: 0 for p in FORBIDDEN}
    results: list[tuple[str, int, list[str]]] = []
    for fname, spec in CHAPTERS.items():
        h, issues = write_chapter(fname, spec)
        body = (PROSE / fname).read_text(encoding="utf-8").split("---")[0]
        for p in FORBIDDEN:
            global_forbidden[p] += body.count(p)
        results.append((fname, h, issues))

    for p, c in global_forbidden.items():
        if c > 1:
            print(f"GLOBAL: {p} appears {c} times across chapters")

    print("=== ch054-062 hz counts ===")
    for fname, h, issues in results:
        flag = " OK" if TARGET_LO <= h <= TARGET_HI else " FAIL"
        extra = f"  {issues}" if issues else ""
