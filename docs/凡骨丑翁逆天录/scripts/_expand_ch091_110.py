def expand_file(path):
    raw = open(path, encoding="utf-8").read()
    m = re.search(r"ch(\d+)", os.path.basename(path))
    key = f"ch{m.group(1)}"
    if key not in EXPANSIONS:
        return
    body, footer = extract_body_and_footer(raw)
    if hz(body) >= TARGET_LO:
        return
    # insert expansion after first blank line following title section (after ~3rd paragraph)
    parts = body.split("\n\n", 3)
    if len(parts) >= 3:
        new_body = parts[0] + "\n\n" + parts[1] + "\n\n" + EXPANSIONS[key].strip() + "\n\n" + "\n\n".join(parts[2:])
    else:
        new_body = body + "\n\n" + EXPANSIONS[key].strip()
    # if still short, append before last paragraph
    if hz(new_body) < TARGET_LO:
        paras = new_body.rsplit("\n\n", 1)
        extra = """
同批少年悄问：「你怎不怕？」

「怕。」韩泥说，「怕也记。记着，手就不飘。」

少年懂「飘」，闭嘴。闭嘴，像丙九也学会忍——忍不是怂，是活路。

执事远廊练嗓，嗓沉：「守规，规在手。」韩泥听半句，不近。

廊下风硬，他把木牌系紧一线。掌心里有瓮醒那路，路不走舌，走手。
"""
        if len(paras) == 2:
            new_body = paras[0] + "\n\n" + extra.strip() + "\n\n" + paras[1]
        else:
            new_body = new_body + "\n\n" + extra.strip()
    title = body.split("\n\n")[0] if body.startswith("#") else ""
    if not title:
        title = raw.split("\n\n")[0]
    content = new_body
    if not content.startswith("#"):
        content = raw.split("\n\n")[0] + "\n\n" + new_body.split("\n\n", 1)[-1] if "#" in raw.split("\n\n")[0] else new_body
    # rebuild from raw title
    title_line = raw.split("\n\n")[0]
    rest = new_body if new_body.startswith("#") else new_body
    if rest.startswith("#"):
        full = rest + "\n\n" + footer.lstrip("\n") if footer else rest
    else:
        full = title_line + "\n\n" + rest + "\n\n" + footer.lstrip("\n") if footer else title_line + "\n\n" + rest
    with open(path, "w", encoding="utf-8") as f:
        f.write(full.strip() + "\n")


def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def main():
    files = sorted(glob.glob(os.path.join(PROSE, "ch09*.md")) + glob.glob(os.path.join(PROSE, "ch10*.md")))
    for p in files:
        expand_file(p)
        # second pass if still short
        expand_file(p)
    print("FILE\tCHARS\tDUP\tOK")
    bad = []
    for p in files:
        raw = open(p, encoding="utf-8").read()
        b, _ = extract_body_and_footer(raw)
        h, d = hz(b), dup(b)
        ok = TARGET_LO <= h <= TARGET_HI and d < 0.02
        print(f"{os.path.basename(p)}\t{h}\t{round(d,4)}\t{ok}")
        if not ok:
            bad.append((os.path.basename(p), h, d))
    if bad:
        print("NEEDS_FIX", bad)
        sys.exit(1)
    print("ALL_OK", len(files))

if __name__ == "__main__":
    main()
