def dup_rate(body):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", body) if len(x.strip()) >= 8]
    if not s:
        return 0.0, []
    seen, d, dups = set(), 0, []
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
            dups.append(x)
        seen.add(k)
    return d / len(s), dups


def wall_paras(body):
    out = []
    for para in re.split(r"\n\s*\n", body):
        t = re.sub(r"\s+", "", para)
        if len(t) > 180:
            out.append((len(t), para[:60] + "..."))
    return out


def hook_tail(body):
    paras = [x.strip() for x in re.split(r"\n\s*\n", body) if x.strip() and not x.startswith("#")]
    return paras[-1] if paras else ""


