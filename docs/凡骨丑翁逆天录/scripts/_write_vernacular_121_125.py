def pack(n: int, body: str) -> str:
    m = META[n]
    body = body.strip() + "\n"
    return (
        body
        + "\n---\n\n章末\n\n"
        + m["footer"]
        + "\n\n"
        + m["status"]
        + "\n"
    )


def qa_one(n: int, text: str):
    body, footer = extract_body_and_footer(text)
    h = hz(body)
    dr, dups = sf.dup_rate(body)
    walls = sf.wall_paras(body)
    q = body.count("「")
    hk = [k for k in sf.HOOK_KEY if k in sf.hook_tail(body)]
    issues = []
    if not (2000 <= h <= 2500):
        issues.append("LEN%d" % h)
    if dr != 0:
        issues.append("dup=%.3f" % dr)
        if dups:
            issues.append("DUP:" + dups[0][:40])
    if walls:
        issues.append("WALL%d" % len(walls))
        issues.append("WMAX%d" % walls[0][0])
    if q < 12:
        issues.append("Q%d" % q)
    if not hk:
        issues.append("NOHOOK")
    if "通俗笔锋" not in footer:
        issues.append("FOOT")
    return h, dr, q, walls, hk, issues, dups


def main():
    results = []
    for n in range(121, 126):
        text = pack(n, BODIES[n])
        h, dr, q, walls, hk, issues, dups = qa_one(n, text)
        tag = "OK" if not issues else "|".join(issues)
        path = os.path.join(PROSE, META[n]["file"])
        if not issues:
            with open(path, "w", encoding="utf-8", newline="\n") as f:
                f.write(text)
            results.append((META[n]["file"], h, q, "WRITTEN"))
            print("%s hz=%d q=%d hook=%s WRITTEN" % (META[n]["file"], h, q, ",".join(hk[:4])))
        else:
            results.append((META[n]["file"], h, q, tag))
            print("%s hz=%d q=%d FAIL %s" % (META[n]["file"], h, q, tag))
            if dups:
                print("  dup sample:", dups[:2])
