def cn_count(text):
    return len(re.findall(r"[\u4e00-\u9fff]", text))

def get_base_bodies():
    """Parse _gen_vol4_part2.py CHAPTERS without running __main__."""
    gen_text = Path(__file__).parent / "_gen_vol4_part2.py"
    content = gen_text.read_text(encoding="utf-8")
    # Strip trailing if __name__ block to avoid side effects
    if 'if __name__ == "__main__"' in content:
        content = content.split('if __name__ == "__main__"')[0]
    ns = {}
    exec(content, ns)
    chapters_text = ns["HEADER"] + "".join(ns["CHAPTERS"])
    parts = re.split(r"(?=### 第)", chapters_text)
    bases = {}
    for p in parts:
        m = re.match(r"### 第(.+?)章 (.+?)\n", p)
        if not m:
            continue
        body = p[m.end():].split("\n---\n")[0].strip()
        for num, cn, title in CHAPTER_META:
            if cn in m.group(1) and title == m.group(2):
                bases[num] = body
                break
    return bases

def dedupe_paragraphs(body):
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    seen = []
    for p in paras:
        if p not in seen:
            seen.append(p)
    return "\n\n".join(seen)

