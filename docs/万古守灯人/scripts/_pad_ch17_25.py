def count(s):
    return len(re.sub(r'\s', '', s.strip()))

def pad_body(body, extras):
    parts = [p.strip() for p in body.strip().split('\n\n') if p.strip()]
    ei = 0
    out = []
    for i, p in enumerate(parts):
        out.append(p)
        if ei < len(extras) and (i % 2 == 1 or i == len(parts) - 1):
            out.append(extras[ei])
            ei += 1
    while ei < len(extras):
        out.append(extras[ei])
        ei += 1
    return '\n\n'.join(out)

