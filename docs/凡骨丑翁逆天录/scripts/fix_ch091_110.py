def strip_filler(text: str) -> str:
    lines = []
    for line in text.splitlines():
        if re.search(r"(丑时第|更残第)\d+息", line):
            continue
        lines.append(line)
    return "\n".join(lines).strip()


def dedupe_paragraphs(body: str) -> str:
    seen, out = set(), []
    for para in re.split(r"\n\s*\n", body):
        p = para.strip()
        if not p:
            continue
        k = re.sub(r"\s+", "", p)
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return "\n\n".join(out)


def dedupe_sentences(body: str) -> str:
    """Remove duplicate sentences (>=8 chars after strip) — matches validation dup()."""
    seen, out = set(), []
    for full in re.split(r"(?<=[。！？])", body):
        s = full.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(s) >= 8 and k in seen:
            continue
        seen.add(k)
        out.append(full)
    return "".join(out).strip()


def sentence_keys(text: str):
    keys = set()
    for sent in re.split(r"(?<=[。！？])", text):
        s = sent.strip()
        if len(s) < 8:
            continue
        k = re.sub(r"\s+", "", s)
        keys.add(k)
    return keys


def git_file(rel: str) -> str:
    try:
        return subprocess.check_output(
            ["git", "-C", ROOT, "show", f"HEAD:{rel}"], encoding="utf-8", stderr=subprocess.DEVNULL
        )
    except subprocess.CalledProcessError:
        return ""


def load_git_body(n: int) -> str:
    if n == 94:
        rel = f"{REL}/{GIT94}"
    else:
        rel = f"{REL}/ch{n:03d}-{NAMES[n]}.md"
    t = git_file(rel)
    if not t:
        return ""
    b, _ = extract_body_and_footer(t)
    b = strip_filler(b)
    lines = b.splitlines()
    if lines and lines[0].startswith("#"):
        b = "\n".join(lines[1:]).strip()
    b = dedupe_paragraphs(b)
    return dedupe_sentences(b)


def trim_git_tail(body: str, drop: int = 6) -> str:
    """Drop trailing hook paragraphs from git base to reduce overlap."""
    if hz(body) < 950:
        return body
    paras = [p for p in re.split(r"\n\s*\n", body) if p.strip()]
    if len(paras) <= drop + 8:
        return body
    return "\n\n".join(paras[:-drop])


def filter_new_block(block: str, seen):
    """Keep only sentences not already in seen."""
    out = []
    for full in re.split(r"(?<=[。！？])", block):
        s = full.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(s) >= 8 and k in seen:
            continue
        seen.add(k)
        out.append(full)
    return "".join(out).strip()


def merge_blocks(body: str, n: int) -> str:
    body = trim_git_tail(body)
    seen = sentence_keys(body)
    for block in (
        MIDDLE.get(n, "").strip(),
        MIDDLE2.get(n, "").strip(),
        MIDDLE3.get(n, "").strip(),
        TOPUP.get(n, "").strip(),
        TOPUP2.get(n, "").strip(),
        MEGA.get(n, "").strip(),
        ULTRA.get(n, "").strip(),
        ULTRA2.get(n, "").strip(),
        ULTRA3.get(n, "").strip(),
    ):
        if block:
            new = filter_new_block(block, seen)
            if new:
                body = body + "\n\n" + new if body else new
    body = dedupe_paragraphs(body)
    body = dedupe_sentences(body)
    return dedupe_sentences(body)


