def inject_topup(body: str, n: int) -> str:
    chunks = [TOPUP[n].strip(), TOPUP2[n].strip(), TOPUP3[n].strip()]
    if TOPUP4.get(n):
        chunks.append(TOPUP4[n].strip())
    if TOPUP5.get(n):
        chunks.append(TOPUP5[n].strip())
    parts = body.rstrip().split("\n\n")
    last = parts[-1]
    head = parts[:-1]
    # keep chrono: core… → topups in order → hook tail
    return "\n\n".join(head + chunks + [last]) + "\n"


def build(n: int) -> str:
    body = inject_topup(BODIES[n].strip(), n)
    # split long paragraphs if any
    paras = []
    for p in body.split("\n\n"):
        t = re.sub(r"\s+", "", p)
        if len(t) <= 180 or p.startswith("#"):
            paras.append(p)
            continue
        # soft split on 。
        buf = ""
        for sent in re.split(r"(?<=。)", p):
            if not sent:
                continue
            trial = re.sub(r"\s+", "", buf + sent)
            if buf and len(trial) > 180:
                paras.append(buf.strip())
                buf = sent
            else:
                buf += sent
        if buf.strip():
            paras.append(buf.strip())
    body = "\n\n".join(paras) + "\n"
    m = META[n]
    return body + "\n---\n\n章末\n\n" + m["footer"] + "\n\n" + m["status"] + "\n"


def qa_one(n: int, text: str):
    body, footer = extract_body_and_footer(text)
    # strip title for some checks but hz includes title per existing scripts
    h = hz(body)
    dr, dups = sf.dup_rate(body)
    walls = sf.wall_paras(body)
    q = body.count("「")
    hk = [k for k in sf.HOOK_KEY if k in sf.hook_tail(body)]
    issues = []
    if not (2000 <= h <= 2500):
        issues.append("LEN%d" % h)
    if dr != 0:
        issues.append("dup=%.3f" % dr)
        if dups:
            issues.append("DUP:" + dups[0][:40])
    if walls:
        issues.append("WALL%d" % len(walls))
        issues.append("WMAX%d" % walls[0][0])
    if q < 12:
        issues.append("Q%d" % q)
    if not hk:
        issues.append("NOHOOK")
    if "通俗笔锋" not in footer:
        issues.append("FOOT")
    for ban in ("恩辱分列", "分列不混", "怒值满", "仙凡笔锋", "药峰分拣", "下一章", "读者若问"):
        if ban in body:
            issues.append("BAN:" + ban)
            break
    # typo check leftover
    if "====" in body:
        issues.append("TYPO")
    return h, dr, q, walls, hk, issues, dups


