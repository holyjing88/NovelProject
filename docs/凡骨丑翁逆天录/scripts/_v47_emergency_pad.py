def pad_chapter(path: str) -> tuple[int, int, int]:
    n = int(re.search(r"ch(\d+)", path).group(1))
    block = PAD.get(n)
    if not block:
        return n, 0, 0
    raw = open(path, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    before = hz(body)
    if before >= GOAL or block.strip() in body:
        return n, before, before
    if END in body:
        head, _ = body.split(END, 1)
        body = head.rstrip() + "\n\n" + block.strip() + END
    else:
        body = body.rstrip() + "\n\n" + block.strip()
    after = hz(body)
    if after < TARGET_LO:
        return n, before, after
    open(path, "w", encoding="utf-8").write(body + footer)
    return n, before, after


def main():
    results = [pad_chapter(p) for p in sorted(glob.glob(os.path.join(PROSE, "ch*.md")))]
    done = [(n, b, a) for n, b, a in results if a and a != b]
    print("PADDED", done)


if __name__ == "__main__":
    main()
