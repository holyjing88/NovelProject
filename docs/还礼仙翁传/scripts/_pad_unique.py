def pad_chapter(name: str) -> int:
    p = PROSE / name
    text = p.read_text(encoding="utf-8")
    body, footer = extract_body_and_footer(text)
    lines = LINES.get(name, []) + LINES2.get(name, []) + LINES3.get(name, []) + LINES4.get(name, []) + LINES5.get(name, []) + LINES6.get(name, [])
    seen = {norm(x) for x in re.split(r"\n\n+", body) if len(norm(x)) >= 20}
    insert_lines = []
    for line in lines:
        n = norm(line)
        if n not in seen and n not in norm(body):
            insert_lines.append(line)
            seen.add(n)
    if insert_lines:
        block = "\n\n".join(insert_lines)
        m = re.search(r"\n\n莫长春袖空，一念收塔", body)
        if m:
            body = body[: m.start()] + "\n\n" + block + body[m.start() :]
        else:
            body = body.rstrip() + "\n\n" + block
    if not footer:
        footer = f"---\n\n*（上架连载稿 · {p.stem.split('-', 1)[1]}）*\n"
    p.write_text(body.rstrip() + "\n\n" + footer, encoding="utf-8")
    return hz(body)


