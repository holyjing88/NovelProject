def insert_before_hr(text, expansion):
    exp = expansion.strip()
    if exp in text:
        return text
    marker = '\n---\n'
    if marker not in text:
        return text
    return text.replace(marker, '\n' + exp + '\n---\n', 1)

for fname, exp in EXPANSIONS.items():
    path = os.path.join(BASE, fname)
    if not os.path.exists(path):
        print('missing:', fname)
        continue
    with open(path, encoding='utf-8') as f:
        text = f.read()
    new = insert_before_hr(text, exp)
    if new != text:
        with open(path, 'w', encoding='utf-8') as f:
            f.write(new)
        print('expanded:', fname)
