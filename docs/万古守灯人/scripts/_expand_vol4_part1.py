def dedupe_paragraphs(text):
    paras = [p.strip() for p in text.split('\n\n') if p.strip()]
    seen, unique = [], []
    for p in paras:
        if p not in seen:
            seen.append(p)
            unique.append(p)
    return '\n\n'.join(unique)

