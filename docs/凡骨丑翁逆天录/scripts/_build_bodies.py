def add_unique_topup(body: str, n: int, target: int = 1550) -> str:
    pool = UNIQUE_TOPUP.get(n, [])
    used = {re.sub(r"\s+", "", s) for s in re.split(r"[。！？\n]", body) if len(s.strip()) >= 4}
    parts = [body.rstrip()]
    for para in pool:
        k = re.sub(r"\s+", "", para)
        if k in used:
            continue
        parts.append(para)
        used.add(k)
    merged = dedupe_body("\n".join(parts))
    if hz(merged) < target and n in EXTRA_UNIQUE:
        for para in EXTRA_UNIQUE[n]:
            k = re.sub(r"\s+", "", para)
            if k in used:
                continue
            merged = dedupe_body(merged.rstrip() + "\n" + para)
            used.add(k)
            if hz(merged) >= target:
                break
    return merged


