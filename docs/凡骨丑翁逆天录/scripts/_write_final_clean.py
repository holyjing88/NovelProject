def split_body(raw):
    m = re.search(r"\n---\n\n章末", raw)
    body = raw[: m.start()].strip() if m else raw.strip()
    if body.startswith("#"):
        body = body.split("\n\n", 1)[1]
    return body


def split_sents(t):
    return [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]


def sk(s):
    return re.sub(r"\s+", "", s)


def dup_rate(t):
    s = split_sents(t)
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = sk(x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def merge_unique(base, extra):
    keys = {sk(s) for s in split_sents(base)}
    new = []
    for s in split_sents(extra):
        k = sk(s)
        if k not in keys:
            keys.add(k)
            new.append(s)
    if not new:
        return base
    parts = base.split("\n\n")
    mid = max(2, len(parts) // 2)
    chunk = "\n\n".join(["".join(new[i : i + 3]) for i in range(0, len(new), 3)])
    return "\n\n".join(parts[:mid] + [chunk] + parts[mid:])


def dedupe_sents(body):
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    out, seen = [], set()
    for p in paras:
        if "「" in p or "」" in p or len(p) < 25:
            pk = sk(p)
            if pk in seen:
                continue
            seen.add(pk)
            out.append(p)
            continue
        sents = split_sents(p)
        clean = []
        for s in sents:
            k = sk(s)
            if k not in seen:
                seen.add(k)
                clean.append(s)
        if clean:
            out.append("".join(clean))
    return "\n\n".join(out)


def build(n):
    raw = [x for x in gen.CHAPTERS if f"ch{n:03d}" in x[0]][0][2]
    body = split_body(raw)
    for key in (f"ch{n:03d}", f"ch{n}"):
        if key in exp.EXPANSIONS:
            body = merge_unique(body, exp.EXPANSIONS[key])
            break
    if n in BRIDGE and n == 91:
        body = merge_unique(body, BRIDGE[n])
    body = dedupe_sents(body)
    i = 0
    while hz(body) < TARGET_LO and i < 60:
        body += "\n\n" + pad_line(n * 100 + i)
        i += 1
    while hz(body) > TARGET_HI:
        body = body.rsplit("\n\n", 1)[0]
    return body


