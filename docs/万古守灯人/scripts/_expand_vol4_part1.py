def cn_num(n):
    return CN[n]

def count_chars(text):
    return len(re.sub(r'\s+', '', text.strip()))

def extract_chapters(content):
    """Extract chapters 141-165, matching longest numeral first."""
    order = sorted(range(141, 166), key=lambda n: -len(cn_num(n)))
    chapters = {}
    for n in order:
        cn = cn_num(n)
        pattern = rf'### 第{cn}章 [^\n]+\n(.*?)(?=\n---)'
        m = re.search(pattern, content, re.DOTALL)
        if m:
            chapters[n] = m.group(1).strip()
    return chapters

