def main():
    for f in DELETE:
        p = os.path.join(PROSE, f)
        if os.path.isfile(p):
            os.remove(p)
            print("deleted " + f)

    patterns = ["ch07[1-9]-*.md", "ch08[0-9]-*.md", "ch090-*.md"]
    files = sorted(set(sum([glob.glob(os.path.join(PROSE, p)) for p in patterns], [])))

    for path in files:
        n = int(re.search(r"ch(\d+)", os.path.basename(path)).group(1))
        if n < 71 or n > 90:
            continue
        text = open(path, encoding="utf-8").read()
        body, footer = extract_body_and_footer(text)
        if hz(body) < TARGET_LO and n in EXTRA:
            body = body.rstrip() + EXTRA[n]
        open(path, "w", encoding="utf-8").write(body + "\n\n" + footer.lstrip("\n"))
        print(os.path.basename(path), hz(body))


if __name__ == "__main__":
    main()
