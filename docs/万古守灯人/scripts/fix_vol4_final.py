def cn_len(s):
    return len(re.sub(r"\s", "", s))


def parse_num(title):
    for k, v in CN_NUM.items():
        if k in title:
            return v
    return 0


def dedupe_paragraphs(body):
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r"\s", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


