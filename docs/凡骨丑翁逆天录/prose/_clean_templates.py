import re, os, glob
base = os.path.dirname(os.path.abspath(__file__))

template = re.compile(
    r'\n\n[^\n]{2,20}风硬，(?:铁无言|邻床)立[^\n]{2,30}侧，声低：「[^」]{2,20}，别飘。飘了，[^」]{2,10}就洒。」韩泥应声：「不飘。[^」]{2,10}在，手在。」手在，就不滚；不滚，[^。\n]{4,30}才接得住。'
    r'\n\n同批少年瞧他忙，忙像沾晦气。韩泥不辩，只记[^—]+——记在心里，不进舌。舌响，香飘；香飘，赖福记「邪」。邪了，[^。\n]+藏不住。'
    r'\n\n刘婆提篮过，篮里饼硬，硬像麸皮停前那口粮。她没多话，只哼：「[^」]{2,20}，手稳。手稳，饭稳。」饭稳，恩不断；恩不断，[^。\n]{4,30}才近半寸。'
    r'\n\n管事夜巡验棚，灯照席角，沿净，记「手稳」。手稳，是小胜；胜在活，不在嘴。嘴赢不得[^，]+，肩赢得过记过。'
    r'\n\n丑时前，他廊下练半刻鼻下静。粟壳香淡，淡不要紧；要紧是香一字不落。落齐了，[^。\n]{4,30}这一日才记进心里，不进舌。'
    r'\n\n赖福脚步在门外停半息：「丑杂役，[^」]{2,20}也配？」韩泥只答：「在。沿净。」在，就不滚。不滚，[^才]{4,30}才开得了。'
    r'\n\n老耿挑水路过，水洒稳，声哑：「[^」]{2,20}别拜暖。拜暖，路断。」韩泥只答：「不拜。坛在，[^。」]{2,15}在。」坛在，根在；根在，就不滚。'
    r'\n\n秦霜远木鱼敲一声：「[^」]{2,20}，心别邪。邪了，鼻下香飘。」韩泥不答，答在心里——心里恩辱分列，不混。混了，心就乱；心乱，手就抖。'
    r'\n\n沈枯芽柱角探头，探头又缩，像怕沾晦气。晦气不要紧，韩泥不近——近，像求；不求，只记[^，]+，记[^，]+，记[^在]+在前头。'
    r'\n\n白织月袖风过，过得冷，没停。没停，小胜——胜在[^，]+，不在嘴；嘴稳，她断的路，叔自己走出来。'
)

for f in glob.glob(os.path.join(base, 'ch09*.md')) + glob.glob(os.path.join(base, 'ch10*.md')):
    with open(f, encoding='utf-8') as fh:
        text = fh.read()
    new = template.sub('', text)
    if new != text:
        with open(f, 'w', encoding='utf-8') as fh:
            fh.write(new)
        print('template removed:', os.path.basename(f))
