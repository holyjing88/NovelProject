def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s: return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen: d += 1
        seen.add(k)
    return d / len(s)

def wall_count(body):
    return sum(1 for p in re.split(r"\n\s*\n", body) if len(re.sub(r"\s+", "", p)) > 180)

def dedupe_body(body):
    seen = set()
    out_paras = []
    for p in re.split(r"\n\s*\n", body):
        if p.strip().startswith("#"):
            out_paras.append(p.strip())
            continue
        if p.strip().startswith("---"):
            break
        sents = []
        for s in re.split(r"(?<=[。！？])", p):
            s = s.strip()
            if not s:
                continue
            k = re.sub(r"\s+", "", s)
            if len(k) >= 8 and k in seen:
                continue
            if len(k) >= 8:
                seen.add(k)
            sents.append(s)
        if sents:
            out_paras.append("".join(sents))
    return "\n\n".join(out_paras)

