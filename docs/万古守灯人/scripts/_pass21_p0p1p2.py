def audit() -> None:
    ok = under = over = tpl = n = 0
    dup_nums: dict[int, int] = {}
    for fp in sorted(CHAPTERS_DIR.glob("vol*.md")):
        for title, body in parse_file(fp.read_text(encoding="utf-8")):
            n += 1
            num = ch_num_from_title(title)
            if num:
                dup_nums[num] = dup_nums.get(num, 0) + 1
            c = han_count(body)
            from _pass20_polish import count_template_hits

            if count_template_hits(body):
                tpl += 1
            if c < MIN_HAN:
                under += 1
            elif c > MAX_HAN:
                over += 1
            else:
                ok += 1
    dups = [k for k, v in dup_nums.items() if v > 1]
    print(f"AUDIT: chapters={n} OK={ok} UNDER={under} OVER={over} TPL={tpl} dup_nums={dups}")


