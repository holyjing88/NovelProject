def insert_block(body: str, block: str) -> str:
    block = block.strip()
    if not block or block in body:
        return body
    if V42_M in body:
        body = re.sub(
            rf"({re.escape(V42_M)}\n\n)(.*?)(?=\n\n{re.escape(END_M)}|\n\n\*\*状态\*\*|\n\n---\n\n章末)",
            lambda m: m.group(1) + m.group(2).rstrip() + "\n\n" + block,
            body,
            count=1,
            flags=re.S,
        )
        return body
    if END_M in body:
        return body.replace(f"\n\n{END_M}", f"\n\n{V42_M}\n\n{block}\n\n{END_M}", 1)
    if "\n\n---\n\n章末" in body:
        return body.replace("\n\n---\n\n章末", f"\n\n{V42_M}\n\n{block}\n\n---\n\n章末", 1)
    return body + f"\n\n{V42_M}\n\n{block}"


def ensure_hook(body: str, n: int) -> str:
    if n not in HOOKS:
        return body
    hook = HOOKS[n]
    if END_M in body:
        body = re.sub(
            rf"{re.escape(END_M)}\s*\n\n.+?(?=\n\n\*\*状态\*\*|\n\n---\n\n章末)",
            f"{END_M}\n\n{hook}",
            body,
            count=1,
            flags=re.S,
        )
        return body
    anchor = "\n\n**状态**" if "**状态**" in body else "\n\n---\n\n章末"
    return body.replace(anchor, f"\n\n{END_M}\n\n{hook}{anchor}", 1)


def ensure_ch_status(body: str, n: int) -> str:
    st = STATUS_OVERRIDE.get(n, default_status(n))
    if n >= 50 and n <= 62 and n not in STATUS_OVERRIDE:
        st = STATUS_OVERRIDE[50]
    if "**状态**" in body:
        body = re.sub(r"\*\*状态\*\*：.+", st, body, count=1)
    else:
        body = ensure_status(body, n)
        if "**状态**" not in body:
            body = body.replace("\n\n---\n\n章末", f"\n\n{st}\n\n\n---\n\n章末", 1)
    return body


def process(path: str) -> tuple[int, int]:
    n = int(re.search(r"ch(\d+)", path).group(1))
    if n not in V42_CHAPTERS:
        return n, 0
    raw = open(path, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    before = hz(body)
    blocks = FILL_ONLY.get(n, [])
    bi = 0
    while hz(body) < TARGET_IDEAL and bi < len(blocks):
        body = insert_block(body, blocks[bi])
        bi += 1
    body = ensure_hook(body, n)
    body = ensure_ch_status(body, n)
    new = body + ("\n\n" + footer if footer else "")
    open(path, "w", encoding="utf-8", newline="\n").write(new)
    return n, hz(body) - before


def main() -> None:
    short = []
    for path in sorted(glob.glob(os.path.join(PROSE, "ch*.md"))):
        n, d = process(path)
        if n not in V42_CHAPTERS:
            continue
        body, _ = extract_body_and_footer(open(path, encoding="utf-8").read())
        h = hz(body)
        if h < TARGET_IDEAL:
            short.append((n, h))
        if d:
            print(f"ch{n:03d} +{d} -> {h}")
    print("still <2000:", short)


if __name__ == "__main__":
    main()
