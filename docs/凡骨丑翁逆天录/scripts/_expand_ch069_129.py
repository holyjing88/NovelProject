def dedupe_paragraphs(body: str) -> str:
    """Remove duplicate paragraphs within body."""
    paras = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for p in paras:
        key = norm(p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


