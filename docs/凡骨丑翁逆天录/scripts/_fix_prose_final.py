def process_chapter(ch: int) -> int:
    pattern = os.path.join(PROSE, f"ch{ch:03d}-*.md")
    files = glob.glob(pattern)
    if not files:
        return -1
    path = sorted(files)[0]
    with open(path, encoding="utf-8") as f:
        text = f.read()
    body, footer = extract_body_and_footer(text)
    lines = body.split("\n")
    title = lines[0] if lines and lines[0].startswith("#") else ""
    body_text = "\n".join(lines[1:]).strip() if title else body
    new_body = expand_body(body_text, ch)
    new_full = (title + "\n\n" + new_body).strip() if title else new_body
    if footer:
        new_full += "\n\n" + footer.lstrip("\n")
    with open(path, "w", encoding="utf-8") as f:
        f.write(new_full + "\n")
    return body_chars(new_full)


def validate():
    missing, short, ok, high_dup = [], [], [], []
    bodies = {}
    for n in range(69, 130):
        files = glob.glob(os.path.join(PROSE, f"ch{n:03d}-*.md"))
        if not files:
            missing.append(n)
            continue
        if len(files) > 1:
            print(f"DUP ch{n}: {[os.path.basename(f) for f in files]}")
        path = sorted(files)[0]
        with open(path, encoding="utf-8") as f:
            text = f.read()
        bc = body_chars(text)
        body, _ = extract_body_and_footer(text)
        bodies[n] = norm(body)
        if bc < TARGET_LO or bc > TARGET_HI:
            short.append((n, bc, os.path.basename(path)))
        else:
            ok.append((n, bc))

    def ngram_sim(a, b, ng=20):
        if len(a) < ng or len(b) < ng:
            return 0.0
        sa = {a[i : i + ng] for i in range(len(a) - ng + 1)}
        sb = {b[i : i + ng] for i in range(len(b) - ng + 1)}
        return len(sa & sb) / len(sa) if sa else 0.0

    keys = sorted(bodies)
    for i, a in enumerate(keys):
        for b in keys[i + 1 :]:
            s = ngram_sim(bodies[a], bodies[b])
            if s >= 0.02:
                high_dup.append((a, b, s))
    return missing, short, ok, high_dup


