def main():
    with open(PATH, 'r', encoding='utf-8') as f:
        text = f.read()
    for num, content in CHAPTERS.items():
        cn_nums = {16:'十六',17:'十七',18:'十八',19:'十九',20:'二十',21:'二十一',22:'二十二'}
        pattern = rf'### 第{cn_nums[num]}章[^\n]*\n.*?(?=\n---\n\n### 第|\n---\n\n> 第二卷)'
        text, n = re.subn(pattern, content.rstrip() + '\n', text, count=1, flags=re.DOTALL)
        cn = len(re.findall(r'[\u4e00-\u9fff]', content))
        print(f'Ch{num}: replaced={n}, chars={cn}')
    with open(PATH, 'w', encoding='utf-8') as f:
        f.write(text)

if __name__ == '__main__':
    main()
