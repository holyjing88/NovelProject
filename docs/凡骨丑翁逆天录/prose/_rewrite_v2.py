def fc(title, body, footer):
    return f"# {title}\n\n{body.strip()}\n\n---\n\n{footer.strip()}\n"


def strip_pollution(body):
    """Remove 第N笔 meta padding and spam blocks."""
    paras = []
    for p in body.split("\n\n"):
        p = p.strip()
        if not p:
            continue
        if re.match(r"^\d+章·第\d+笔", p):
            continue
        if re.search(r"第\d+次|第\d+更|第\d+回", p) and any(
            x in p for x in ("刘婆端粥", "更鼓将尽", "管事夜巡", "赖福脚步", "老耿挑水")
        ):
            continue
        if p.count("更鼓将尽，天边白一线") > 1:
            continue
        if re.search(r"(丑时第|更残第)\d+息", p):
            continue
        paras.append(p)
    return "\n\n".join(paras)


def dedupe_sentences_light(body):
    """Remove exact duplicate sentences only; preserve paragraphs."""
    out, seen = [], set()
    for para in body.split("\n\n"):
        para = para.strip()
        if not para:
            continue
        parts = re.split(r"(?<=[。！？])", para)
        clean = []
        for s in parts:
            s = s.strip()
            if not s:
                continue
            k = re.sub(r"\s+", "", s)
            if len(k) >= 8 and k in seen:
                continue
            if len(k) >= 8:
                seen.add(k)
            clean.append(s)
        if clean:
            out.append("".join(clean))
    return "\n\n".join(out)


def dedupe_paragraphs(body):
    seen, out = set(), []
    for p in body.split("\n\n"):
        p = p.strip()
        if not p:
            continue
        k = re.sub(r"\s+", "", p)
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return "\n\n".join(out)


def split_walls(body, mx=175):
    out = []
    for para in body.strip().split("\n\n"):
        if not para.strip():
            continue
        if hz(para) <= mx:
            out.append(para.strip())
            continue
        buf = ""
        for sent in re.split(r"(?<=[。！？])", para):
            if not sent.strip():
                continue
            if hz(buf + sent) > mx and buf.strip():
                out.append(buf.strip())
                buf = sent
            else:
                buf += sent
        if buf.strip():
            out.append(buf.strip())
    return "\n\n".join(out)


def load_batch(path):
    """Parse CHAPTERS from batch file without executing."""
    text = open(path, encoding="utf-8").read()
    chapters = {}
    pat = re.compile(
        r"CHAPTERS\['(ch\d+[^']+)'\]\s*=\s*full_chapter\(\s*\"([^\"]+)\"\s*,\s*'''(.*?)'''\s*,\s*'''(.*?)'''\s*\)",
        re.S,
    )
    for m in pat.finditer(text):
        chapters[m.group(1)] = fc(m.group(2), m.group(3), m.group(4))
    return chapters


