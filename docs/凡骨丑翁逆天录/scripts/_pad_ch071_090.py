def dedupe_sentences(text):
    sents = re.split(r"(?<=[。！？])", text)
    seen, out = set(), []
    for s in sents:
        s = s.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(k) >= 8 and k in seen:
            continue
        if len(k) >= 8:
            seen.add(k)
        out.append(s)
    return "".join(out)


def main():
    for dup in DELETE_DUPES:
        p = os.path.join(PROSE, dup)
        if os.path.isfile(p):
            os.remove(p)
            print(f"deleted {dup}")

    for n, fn in CANONICAL.items():
        path = os.path.join(PROSE, fn)
        if not os.path.isfile(path):
            print(f"MISSING {fn}")
            continue
        text = open(path, encoding="utf-8").read()
        body, footer = extract_body_and_footer(text)
        body = body.rstrip()
        pad = PAD.get(n, "").strip()
        if pad:
            body = body + "\n\n" + pad
        body = dedupe_sentences(body)
        while hz(body) < TARGET_LO:
            extra = f"\n\n更鼓沉，沉里，韩泥按木牌，牌凉，掌温。掌温在{n}，不在舌上。舌响，香飘；香飘，手就滚。滚了，恩断。他不滚，手在，名在，路在。"
            body = body + extra
            body = dedupe_sentences(body)
        new_text = body + "\n\n" + footer.lstrip("\n")
        open(path, "w", encoding="utf-8").write(new_text)
        print(f"ch{n:03d} {fn} hz={hz(body)}")


if __name__ == "__main__":
    main()
