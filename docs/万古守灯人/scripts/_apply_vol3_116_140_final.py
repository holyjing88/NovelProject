def dedupe_paragraphs(text):
    paras = [p.strip() for p in re.split(r"\n\s*\n", text) if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r"\s+", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


def limit_phrase(text, phrase, max_count=1):
    """Keep only first max_count occurrences of phrase-containing paragraphs."""
    paras = text.split("\n\n")
    count = 0
    out = []
    for p in paras:
        if phrase in p:
            count += 1
            if count > max_count:
                continue
        out.append(p)
    return "\n\n".join(out)


