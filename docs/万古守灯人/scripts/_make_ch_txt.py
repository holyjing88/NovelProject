def count(s):
    return len(re.sub(r'\s', '', s))

def write_ch(n, paras):
    body = '\n\n'.join(paras)
    c = count(body)
    if c < 3500:
        raise SystemExit(f'ch{n}: only {c} chars — add unique scenes, no PAD loop')
    (OUT / f'ch{n:02d}.txt').write_text(body + '\n', encoding='utf-8')
    return c

