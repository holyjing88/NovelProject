def expand_chapter(n: int) -> None:
    path = os.path.join(PROSE, f"ch{n:03d}-*.md")
    matches = glob.glob(path)
    if not matches:
        print(f"MISSING ch{n}")
        return
    fpath = matches[0]
    raw = open(fpath, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    add = EXPANSIONS.get(n, "").strip()
    if not add or add in body:
        return
    # Insert before --- footer
    if "\n---\n\n章末" in body:
        body = body.replace("\n---\n\n章末", f"\n{add}\n\n---\n\n章末", 1)
    else:
        body = body + "\n" + add
    new_raw = body + "\n\n" + footer if footer else body
    open(fpath, "w", encoding="utf-8").write(new_raw)
    h = hz(body)
    print(f"ch{n:03d}: {h} chars")

