def extract_chapters(text):
    chapters = {}
    pattern = re.compile(r"(### 第(.+?)章 [^\n]+)\n\n(.*?)(?=\n---\n|\Z)", re.S)
    for m in pattern.finditer(text):
        title_cn = m.group(2).strip()
        rev = {v: k for k, v in CN.items()}
        num = rev.get(title_cn)
        if num and 116 <= num <= 140:
            chapters[num] = (m.group(1), m.group(3).strip())
    return chapters


def limit_phrase(text, phrase, max_count=1):
    paras = text.split("\n\n")
    count = 0
    out = []
    for p in paras:
        if phrase in p:
            count += 1
            if count > max_count:
                continue
        out.append(p)
    return "\n\n".join(out)


