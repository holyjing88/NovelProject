    start116 = content.find("### 第一百一十六章")
    footer = content.find("> 第四卷")
    part115 = content[:start116].rstrip()
    old_section = content[start116:footer]

    headers = re.findall(r"### 第[^\n]+", old_section)
    rebuilt = []

    for h in headers:
        num = cn_num(h)
        if num is None or num > 137:
            continue
        idx = old_section.find(h)
        nidx = old_section.find("### 第", idx + 5)
        chunk = old_section[idx:nidx if nidx > 0 else len(old_section)]
        body = re.sub(r"^###[^\n]+\n+", "", chunk).strip()
        body = expand_to_target(body, num)
        rebuilt.append(f"{h}\n\n{body}\n\n---\n")

    new_section = "\n".join(rebuilt).rstrip() + "\n\n"
    for ch_text, num in [(CH138, 138), (CH139, 139), (CH140, 140)]:
        h = ch_text.split("\n")[0]
        raw = re.sub(r"^###[^\n]+\n+", "", ch_text).strip()
        body = expand_to_target(raw, num, skip_extra=(num == 140))
        if num == 140:
            if "**第三卷完**" in body:
                idx = body.index("**第三卷完**") + len("**第三卷完**")
                body = body[:idx].strip()
            else:
                body += "\n\n**第三卷完**"
        new_section += f"{h}\n\n{body}\n\n---\n\n"
    new_section = new_section.rstrip() + "\n\n"

    new_content = part115 + "\n\n" + new_section + content[footer:]

    with open(PATH, "w", encoding="utf-8") as f:
        f.write(new_content)

    # counts
    all_h = re.findall(r"### 第[^\n]+", new_section)
    total = 0
    for h in all_h:
        i = new_section.find(h)
        j = new_section.find("### 第", i + 5)
        t = re.sub(r"^###[^\n]+\n+", "", new_section[i:j if j > 0 else len(new_section)]).strip()
        t = re.sub(r"\n---\s*$", "", t).replace("**第三卷完**", "").strip()
        n = len(t)
        total += n
        flag = "OK" if 2500 <= n <= 4500 else ("LOW" if n < 2500 else "HIGH")
        print(f"{flag} {n:5d}  {h}")
    print(f"TOTAL 116-140: {total}")


