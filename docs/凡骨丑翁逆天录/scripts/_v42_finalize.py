def dedupe_thicken(body: str) -> str:
    m = re.search(r"<!-- v38-thicken -->(.*?)(?=<!-- v38-topup -->|<!-- v42-topup -->|<!-- v38-end -->|\*\*状态\*\*|\n\n---\n\n章末)", body, re.S)
    if not m:
        return body
    th = m.group(1)
    chunks = [c.strip() for c in SPLIT.split(th) if len(c.strip()) >= 8]
    seen: set[str] = set()
    out: list[str] = []
    for c in chunks:
        k = re.sub(r"\s+", "", c)
        if k in seen:
            continue
        seen.add(k)
        out.append(c)
    new_th = "".join(out)
    return body[: m.start(1)] + new_th + body[m.end(1) :]


def insert_sentences(body: str, sentences: list[str]) -> str:
    new_sents = [s for s in sentences if s not in body]
    if not new_sents:
        return body
    block = "".join(new_sents)
    if V42_M in body:
        body = re.sub(
            rf"({re.escape(V42_M)}\n\n)(.*?)(?=\n\n{re.escape(END_M)}|\n\n\*\*状态\*\*|\n\n---\n\n章末)",
            lambda m: m.group(1) + (m.group(2).rstrip() + "\n\n" + block if m.group(2).strip() else block),
            body,
            count=1,
            flags=re.S,
        )
    elif END_M in body:
        body = body.replace(f"\n\n{END_M}", f"\n\n{V42_M}\n\n{block}\n\n{END_M}", 1)
    elif "\n\n---\n\n章末" in body:
        body = body.replace("\n\n---\n\n章末", f"\n\n{V42_M}\n\n{block}\n\n---\n\n章末", 1)
    else:
        body += f"\n\n{V42_M}\n\n{block}"
    return body


def fix_tail(body: str, n: int) -> str:
    # 去掉错位状态/钩，重建尾段
    body = re.sub(r"\n\n\*\*状态\*\*：.+", "", body)
    body = re.sub(r"\n\n<!-- v38-end -->.*?(?=\n\n---\n\n章末|\Z)", "", body, flags=re.S)
    body = body.rstrip()
    hook = HOOKS.get(n, "")
    if hook:
        body += f"\n\n{END_M}\n\n{hook}"
    st = STATUS.get(n, DEFAULT_ST if 50 <= n <= 62 else DEFAULT_ST.replace("瓮温加剧）", "眠）"))
    if n < 50:
        st = "**状态**：大境·凡人 · 资质·丑骨末席（伪灵根） · 鸿蒙九劫瓮·眠 · 洞府·漏舍凡舍"
    body += f"\n\n{st}"
    return body


def dedupe_body(body: str) -> str:
    chunks = [c.strip() for c in SPLIT.split(body) if c.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for c in chunks:
        k = re.sub(r"\s+", "", c)
        if k in seen:
            continue
        seen.add(k)
        out.append(c)
    return "".join(out)


def process(path: str) -> None:
    n = int(re.search(r"ch(\d+)", path).group(1))
    if n not in V42_CHAPTERS:
        return
    raw = open(path, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    body = dedupe_thicken(body)

    all_sents = BOOST.get(n, []) + EXTRA2.get(n, [])
    for i in range(0, len(all_sents), 1):
        if hz(body) >= TARGET_IDEAL:
            break
        body = insert_sentences(body, [all_sents[i]])

    if dup_ratio(body) >= 0.02:
        body = dedupe_body(body)

    if n in HOOKS or n in STATUS or 50 <= n <= 62:
        body = fix_tail(body, n)
    elif hz(body) < TARGET_IDEAL and n in EXTRA2:
        # 近 2000 的泥岗/宗门章只补句，不改尾
        pass

    new = body + ("\n\n" + footer if footer else "")
    open(path, "w", encoding="utf-8", newline="\n").write(new)


def dup_ratio(t: str) -> float:
    s = [x.strip() for x in SPLIT.split(t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def main() -> None:
    short = []
    for path in sorted(glob.glob(os.path.join(PROSE, "ch*.md"))):
        process(path)
