def strip_templates(body: str) -> str:
    paras = [
        p
        for p in split_paras(remove_duplicate_templates_only(body))
        if not is_template_para(p)
    ]
    return dedupe_paragraphs("\n\n".join(paras))


def best_base(num: int, current: str) -> str:
    if num == 58:
        base = CH58.strip()
    elif 61 <= num <= 69:
        parts = [build_tower_body(num, "").strip()]
        if num in GIT_VOL2:
            extras = story_paras(GIT_VOL2[num])
            if extras:
                parts.append("\n\n".join(extras))
        base = dedupe_paragraphs("\n\n".join(parts))
    else:
        return strip_templates(current)
    return base


def patch_body(num: int, current: str) -> str:
    body = best_base(num, current).strip()
    for pad in (MANUAL_TAIL, FINAL_PAD, EXTRA_PAD, MICRO_PAD):
        if num in pad:
            body = dedupe_paragraphs(body + "\n\n" + pad[num].strip())
    if han_count(body) > MAX_HAN:
        body = trim_body(body)
    return body.strip() + "\n"


def main() -> None:
    text = VOL2.read_text(encoding="utf-8")
    chunks = parse_file(text)
    new_chunks = []
    for title, body in chunks:
        num = ch_num_from_title(title)
        if num in TARGETS:
            body = patch_body(num, body)
            print(f"  ch{num}: {han_count(body)} han tpl={count_template_hits(body)}")
        new_chunks.append((title, body))
    VOL2.write_text(rebuild(text, new_chunks), encoding="utf-8")

    ok = under = tpl = 0
    for title, body in parse_file(VOL2.read_text(encoding="utf-8")):
        num = ch_num_from_title(title)
        if num not in TARGETS:
            continue
        c = han_count(body)
        t = count_template_hits(body)
        if t:
            tpl += 1
        if c < MIN_HAN:
            under += 1
        else:
            ok += 1
    print(f"AUDIT targets: OK={ok} UNDER={under} TPL={tpl}")


if __name__ == "__main__":
    main()
