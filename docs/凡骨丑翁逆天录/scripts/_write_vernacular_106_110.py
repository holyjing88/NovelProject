def check(body: str) -> tuple[bool, str]:
    h = hz(body)
    quotes = len(re.findall(r"「[^」]*」", body))
    blocks = []
    buf = []
    for line in body.split("\n"):
        if line.strip() == "":
            if buf:
                blocks.append("\n".join(buf))
                buf = []
        else:
            buf.append(line)
    if buf:
        blocks.append("\n".join(buf))
    maxb = max((hz(b) for b in blocks), default=0)
    over = [hz(b) for b in blocks if hz(b) > 180]
    sents = re.findall(r"[^。！？\n]+[。！？]", body)
    c = Counter(
        s.strip()
        for s in sents
        if len(re.findall(r"[\u4e00-\u9fff]", s)) >= 8
    )
    dups = [(k, v) for k, v in c.items() if v >= 2]
    ok = (
        TARGET_LO <= h <= TARGET_HI
        and quotes >= 12
        and maxb <= 180
        and len(dups) == 0
        and not over
    )
    msg = "hz=%d quotes=%d max段=%d dup=%d over=%s" % (
        h,
        quotes,
        maxb,
        len(dups),
        over[:3],
    )
    if dups:
        msg += " DUP0=%s" % dups[0][0][:40]
    return ok, msg


