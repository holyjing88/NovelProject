def hz(s: str) -> int:
    return len(re.findall(r"[\u4e00-\u9fff]", s))


def dup_ratio(t: str) -> float:
    sents = [s.strip() for s in re.split(r"[。！？\n]", t) if len(s.strip()) >= 4]
    if not sents:
        return 0.0
    seen, dup = set(), 0
    for s in sents:
        if s in seen:
            dup += 1
        seen.add(s)
    return dup / len(sents)


def has_sent(body: str, sent: str) -> bool:
    used = {re.sub(r"\s+", "", s) for s in re.split(r"[。！？\n]", body) if len(s.strip()) >= 4}
    return re.sub(r"\s+", "", sent) in used


