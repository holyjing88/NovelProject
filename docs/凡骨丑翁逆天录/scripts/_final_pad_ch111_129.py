fail = []
for n in range(111, 130):
    p = glob.glob(os.path.join(PROSE, f"ch{n:03d}-*.md"))[0]
    body, _ = extract_body_and_footer(open(p, encoding="utf-8").read())
    h, hd = hz(body), dup(body)
    if not (TARGET_LO <= h <= TARGET_HI and hd < 0.02):
        fail.append((n, h, round(hd, 3)))
        print(f"ch{n:03d}: {h} dup={hd:.3f} FAIL")
if not fail:
    print("ALL_OK")
