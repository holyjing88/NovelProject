def strip_templates(body: str) -> str:
    paras = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
    out = []
    for p in paras:
        if any(m in p for m in TEMPLATE_MARKERS):
            continue
        out.append(p)
    return "\n\n".join(out)


def dedupe_paragraphs(body: str) -> str:
    paras = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for p in paras:
        key = norm(p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


def expand_body(body: str, ch: int) -> str:
    body = dedupe_paragraphs(strip_templates(body))
    blocks = list(EXPAND.get(ch, [])) + list(UNIQUE.get(ch, []))
    seq = 0
    while body_chars(body) < TARGET_LO and seq < len(blocks):
        paras = body.split("\n\n")
        pos = min(len(paras), 1 + (seq % max(1, len(paras))))
        if blocks[seq] not in paras:
            paras.insert(pos, blocks[seq])
            body = dedupe_paragraphs("\n\n".join(paras))
        seq += 1
    if body_chars(body) > TARGET_HI:
        paras = body.split("\n\n")
        while body_chars("\n\n".join(paras)) > TARGET_HI and len(paras) > 6:
            paras.pop(-2)
        body = "\n\n".join(paras)
    return body


