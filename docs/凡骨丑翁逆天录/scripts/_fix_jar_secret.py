def main():
    files = []
    for n in range(52, 63):
        for f in os.listdir(base):
            if f.startswith("ch%03d-" % n) and f.endswith(".md"):
                files.append(os.path.join(base, f))
    applied = 0
    for path in files:
        raw = open(path, encoding="utf-8").read()
        new = raw
        for a, b in repls + narr:
            if a in new:
                new = new.replace(a, b)
                applied += 1
        # leftover dialogue keywords cleanup for 他人言及主动滴
        if new != raw:
            open(path, "w", encoding="utf-8", newline="\n").write(new)
            print("updated", os.path.basename(path))
    print("replacements", applied)

    # report leftovers
    for path in files:
        raw = open(path, encoding="utf-8").read()
        hits = []
        for kw in ("主动滴", "不主动滴", "误那一甩", "活计误", "血在席，不在沿"):
            if kw in raw:
                hits.append(kw)
        if hits:
            print("LEFTOVER", os.path.basename(path), hits)


if __name__ == "__main__":
    main()
