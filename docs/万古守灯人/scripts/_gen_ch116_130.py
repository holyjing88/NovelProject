def write_file(chapters):
    lines = ['# -*- coding: utf-8 -*-', '"""Volume 3 chapters 116-140 content bodies."""', '', 'CHAPTERS = {']
    for n in sorted(chapters):
        title, body = chapters[n]
        lines.append(f'    {n}: ({repr(title)},')
        lines.append(f'"""{body.strip()}"""),')
    lines.append('}')
    lines.append('')
    OUT.write_text('\n'.join(lines), encoding='utf-8')

