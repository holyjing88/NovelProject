SPLIT_DEDUP = re.compile(r"(?<=[。！？])")


def dedupe_sents(text: str) -> str:
    chunks = [c.strip() for c in SPLIT_DEDUP.split(text) if c.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for c in chunks:
        key = re.sub(r"\s+", "", c)
        if key in seen:
            continue
        seen.add(key)
        out.append(c)
    return "".join(out)

