def count_cn(s):
    return len(re.findall(r'[\u4e00-\u9fff]', s))

def main():
    text = PATH.read_text(encoding='utf-8')
    for num in range(16, 41):
        cn = CN[num]
        pattern = rf'(### 第{cn}章[^\n]*\n)(.*?)(\n---\n)'
        m = re.search(pattern, text, flags=re.DOTALL)
        if not m:
            continue
        body = m.group(2)
        cur = count_cn(body)
        if cur >= TARGET:
            print(f'Ch{num:02d}: {cur} OK')
            continue
        extra = EXTRA.get(num, '')
        if extra and extra not in body:
            body = body + '\n' + extra
        new = count_cn(body)
        text = text[:m.start()] + m.group(1) + body + m.group(3) + text[m.end():]
        print(f'Ch{num:02d}: {cur} -> {new}')
    PATH.write_text(text, encoding='utf-8')

if __name__ == '__main__':
    main()
