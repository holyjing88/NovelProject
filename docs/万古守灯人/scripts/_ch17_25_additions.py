add("第十七章 赵家逼婚", """红帖贴到药铺门上的那天，青萝镇半条街都来看。

帖用朱砂写成，字迹张扬，末尾盖的不是赵家铺印，而是一枚云岚宗外门执事的窄印。印一现，围观人群里立刻有人倒吸凉气——这不是地痞闹事，是"宗门意思"。

红帖四周，有人窃笑，有人叹息。沈家药铺在青萝镇开了两代，若真被赵家连根拔起，后巷仓基改作赵家药仓，全镇药价便由赵家说了算。

更有人窃窃私语："陆首席的人来了，沈家这回躲不过。"

沈青禾开门，看见红帖，手在门闩上停住，指节一点点白。她丈夫过世不足两载，赵家逼债未成，竟改逼婚，帖上每一个字都像刀，剜在牌匾"沈氏药铺"四字上。

赵元青扫一眼人群，像在挑谁会被吓退。他锦氅下手指微颤——保释不易，今日若再失手，陆承安亦保不住他。

帖上写得很明白：赵元青虽收监，赵家与沈家"旧债新恩"，愿以聘礼百两、后巷仓基一契，纳沈青禾为妾；若不从，另有"仙缘"——沈氏女有灵药天赋，可持荐书入云岚宗内门，身份是"药奴"，非弟子。

"做妾，或者入宗为奴。"传帖的家丁扯着嗓子念，像怕镇上人听不清，"沈掌柜，你选。"

顾迟年站定，不挡沈青禾，只挡在帖与刀之间。书吏不佩剑，袖中有账，账中有名，名中有刀。

沈青禾没有选。她回身从柜下抽出短刀，刀锋出鞘半寸，寒光映在她眼里，手却抖。

"沈掌柜要动刀？"家丁退半步，仍笑，"动了刀，正好坐实你心虚。赵少爷说了，你不动，还有路；你动，连药铺牌匾都保不住。"

跟班欲再展荐书，温言脚步声已近。赵元青眼底一沉，知道今日当街已难成，只能改日再逼。

沈青禾咬唇，刀尖微颤。她想起顾迟年说的"今天不流血"，可血若不在今日流，明日流的可能就是名节。

就在此时，门外人群分开一条路。顾迟年自街角来，不快不慢，手里只持一册账，蓝布封面，像寻常书吏随身簿。他昨夜告假未批，仍是下了山；陆承安若查，便是擅离之罪。

沈青禾刀入鞘后，手仍白。她看顾迟年，忽然问：'你告假未批，回宗怎交代？'顾迟年答：'交代在堂上，不在口头。'

铁柱在两条街外把风，顾迟年独自入镇，换的是书吏青衫，不是云岚杂役服。赵家盯宗门，未必盯青衫。

"赵少爷。"他声音平静，"你要沈家，还是要你赵家私账？"

雨意渐浓，红帖被雨水晕开，像血。里正命人揭帖暂存，待堂上定罪后焚。人群散时，仍有人朝药铺吐唾，被更夫喝止。

赵元青竟也在。他从监外保释出来，披着锦氅，眼下青黑，却仍摆少爷派头。身后跟着陆承安的一名外门跟班，跟班袖中露出一角荐书，黄绫边，云岚宗内门样式，上头墨迹新，像刚签。

"顾老头，你又下山。"赵元青笑，眼里无笑，"宗门告假未批，你擅离，按例当罚。"

顾迟年回药铺取姜汤，沈青禾隔桌递碗，二人之间，像隔着一宗未结的案。

"罚我，稍后再说。"顾迟年把账册抬了半寸，"这册子，先说。"

赵元青脸色微变："什么册子？"

"你私账。"顾迟年语气仍平，"逼债条目，逼婚条目，买通地痞条目，勾结内门条目。还有一条——借霍家旁支青衫修士压场，却未付约定分成，霍家那边，怕也要问你话。"

围观者哗然。有人低声："他怎知道得这么细？"

赵元青强撑："书吏造谣，谁信？"

"信不信，不在嘴，在灯。"顾迟年抬眼，"你要现在对质，我奉陪。你要现在动手，我也奉陪。但你要想清楚——沈家今日若倒，明日倒的就是赵家整条线。霍家旁支借刀，刀折了，借刀的人最先见血。"

赵元青袖中手一紧，跟班却上前半步，冷声："顾迟年，陆首席有言：沈青禾天赋既出，宗门要人，俗世纠纷，少掺和。"

他从袖中取出荐书，在半空展开一截，只让人看见云岚宗朱印，却遮了正文："此荐书可入内门药庐，多少人求而不得。沈掌柜别不知好歹。"

"宗门要人，走宗门律。"顾迟年不退，"持荐书逼婚，是揽才，还是揽地？荐书上写没写'药奴'二字？写写了，内门哪位长老签的字？"

跟班一滞，目光闪向赵元青。

赵元青接话："荐书是陆首席美意，沈青禾入宗炼药，比在这破铺子里熬一辈子强。"

"强？"沈青禾忽然冷笑，"强到连弟子名分都不给，只给奴籍？陆首席若真惜才，为何不走正途收徒，要走逼婚逼地的歪路？"

跟班脸色一沉："沈掌柜，慎言。陆首席清誉，岂容你污蔑？"

"我污蔑？"沈青禾刀尖微抬，"红帖贴门，奴籍逼签，全镇作见证——这算污蔑，还是算你们赵家与内门联手做局？"

人群里有人点头，有人仍惧赵家，只敢退后半步。里正挤进来，搓手陪笑："赵少爷，顾先生，有话好说，别当街——"

"里正。"顾迟年打断，"今日不说好说。说账。"

赵元青见势不对，转而攻心："顾迟年，你护她，护到什么时候？你迟暮之年，自身难保，还要拖一个寡妇下水？"

"拖下水的是你们。"沈青禾开口，声音清亮，"我丈夫没借赵家三十两。我若做妾，是卖身；我若做奴，是卖名。两样都不卖。"

"好。"赵元青怒极反笑，"不卖，那就别怪赵家不客气——"

他抬手，家丁要上，跟班却退半步，像不愿沾血。

顾迟年把账册合拢，轻轻一拍。那一拍不重，袖中守岁灯却似被什么引动，微光在白日里跳了一跳。

"赵少爷。"顾迟年道，"最后一问。你要现在在这里把私账认下，还是等温言巡街，到县衙认？"

赵元青瞳孔一缩。

跟班低声："赵少爷，陆首席说，别在当街闹大——"

"闹大的是你们。"顾迟年把账册翻开一页，只给赵元青看，"这一页，买通内门管事，换荐书一枚。签名的笔迹，陆首席认得，你也认得。"

赵元青额角青筋一跳，忽然笑："顾迟年，你拿一本假账吓唬我？"

"假不假，明日堂上见。"顾迟年合账，"今日我只问一句：逼婚帖，撤不撤？"

"不撤。"

"那便留着。"顾迟年退半步，"留着给温言看。"

街角，忽然传来整齐脚步声。温言带两名捕快转进巷口，佩刀在腰，目光如刀："谁在当街逼婚？"

赵元青脸色彻底沉了。

顾迟年把路让出来，像只是把舞台交给该在的人。他侧首对沈青禾极低一句："刀收。今天不流血。"

沈青禾深吸一口气，刀入鞘。

赵元青被温言扣住手腕时，仍死死盯顾迟年："你有种。万灯大会，我看你怎么死。"

顾迟年没回嘴，只抬眼望天。天阴，像要落雨。

温言押人走，人群渐散。沈青禾关半边门，低声问："册子……真是赵家私账？"

"真有一半。"顾迟年诚实，"假有一半。"

沈青禾一怔。

"书吏办事，从不把底牌一次亮完。"顾迟年把账册收回袖中，"要钉死赵元青，册子不够，要堂上开灯。"

"二阶？"

"是。"

沈青禾沉默良久，道："代价呢？"

顾迟年看她，没瞒："一炷香，看不见。"

她指尖又白了一分，却只说："姜汤在灶上。你堂上回来，还热。"

顾迟年没应"好"，只道："记着。"

他转身往县衙方向走，袖中守岁灯微温。他知道，册子上的账，还不足以钉死赵元青。要钉，需要火——二阶烛火。

而烛火，有代价。

雨落一滴，砸在青石板上，像更夫提前敲了梆。里正追上来，欲言又止，最终只道："顾先生，明日升堂，全镇都看着呢。"

"看着好。"顾迟年没回头，"有人看着，灯才亮。"

赵家铺子方向，跟班匆匆离去，荐书收回袖中，像一条未断的线，仍系在陆承安指间。顾迟年心知：逼婚是明枪，荐书是暗索。明枪温言可挡，暗索还得靠烛火照一照。

药铺内，沈青禾把红帖撕下，碎纸入炉，火起即灭。她对面站着，忽然问："你册子里，买通内门那条，是真？"

"真。"顾迟年不瞒，"陆承安要沈家药庐，赵家要仓基，各取所需。你，是筹码。"

沈青禾指尖一颤，随即稳了："那我更不做妾，更不做奴。"

"你不做，他们便强。"顾迟年把账册递她看一眼，"强，也要在堂上强。堂下动刀，你亏。"

沈青禾深吸一口气："我明白。"

"姜汤。"顾迟年指灶，"我半个时辰后回。若温言来，请她内堂坐，别让人看笑话。"

"你往哪？"

"县衙。"顾迟年道，"先把人证序好。瘸腿王、船工、更夫，里正都已约。你只管睡半个时辰。"

"我睡不着。"

"睡不着也闭眼。"顾迟年难得语气硬了一分，"明日堂上，你要站直。"

沈青禾没再辩，只转去盛姜汤，仍隔三步递碗。

顾迟年在县衙外廊下与温言碰头。温言开门见山："赵元青保释，是上面有人压。我扣得住一时，扣不住一世。"

"一世不用。"顾迟年说，"一炷香就够。"

温言盯他袖："你要开二阶？"

"是。"

"失明一炷香，你可想过回宗的路？"

"想过。"顾迟年平静，"瞎着走，铁柱在林口。看得见时，我在堂上。"

温言沉默片刻，道："霍家旁支那青衫，我会留。你照你的，我抓我的。"

"合则破。"

"合。"

二人击掌，轻，却实。

半日后，县衙钟响。顾迟年提灯在阶下，灯芯未燃，心已先稳了一分。

他抬眼，见沈青禾立在人群最后，没靠近，只遥遥一点头。

他亦点头。

这一点头，像把两碗姜汤、一册私账、一炷香失明，一并押上了堂。

堂鼓三响，赵元青被押上，枷锁碰地，声脆。跟班未至，陆承安的荐书却作为证物呈上，黄绫边在风里微动，像一条未剪的索。

温言问顾迟年："开灯，有几成？"

"七成。"顾迟年答，"够钉赵元青，够引出霍家旁支。不够钉陆承安。"

"陆承安不在今日。"温言说，"在日后。"

"日后我记。"顾迟年提灯上阶，"今日，只照赵家。"

沈青禾在人群里，指节发白，却没退。她知道，下一炷香，顾迟年看不见她——可她看得见他。

这就够了。""")

