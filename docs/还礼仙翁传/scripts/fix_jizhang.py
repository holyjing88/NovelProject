def main() -> None:
    changed_files: list[tuple[str, int]] = []
    for i in range(1, 62):
        matches = sorted(PROSE.glob(f"ch{i:03d}-*.md"))
        if not matches:
            continue
        path = matches[0]
        text = path.read_text(encoding="utf-8")
        orig = text
        for old, new in REPLACEMENTS:
            text = text.replace(old, new)
        if text != orig:
            path.write_text(text, encoding="utf-8")
            changed_files.append(path.name)
    print(f"updated {len(changed_files)} files")
    for name in changed_files:
        print(f"  {name}")
    # 残留检查
    remain = []
    for i in range(1, 62):
        matches = sorted(PROSE.glob(f"ch{i:03d}-*.md"))
        if not matches:
            continue
        t = matches[0].read_text(encoding="utf-8")
        for pat in ("记账", "记帐", "入账", "落账", "记一笔", "门账", "册上记"):
            if pat in t:
                remain.append((matches[0].name, pat))
    if remain:
        print("REMAINING:")
        for name, pat in remain:
            print(f"  {name}: {pat}")
    else:
        print("no 记账/记帐 remaining in ch001-061")


if __name__ == "__main__":
    main()
