def insert(raw, block):
    m = re.search(r"\n---\n\n章末", raw)
    body, tail = raw[:m.start()].rstrip(), raw[m.start():]
    return body + "\n" + block.strip() + "\n" + tail

def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s: return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen: d += 1
        seen.add(k)
    return d / len(s)

