def expand_file(n: int) -> tuple[int, int, float]:
    path = next(PROSE.glob(f"ch{n:03d}-*.md"))
    text = path.read_text(encoding="utf-8")
    body, footer = extract_body_and_footer(text)
    before = body_chars(text)

    marker = "\n---\n\n章末"
    if marker not in text:
        raise ValueError(f"ch{n:03d}: footer marker missing")

    head, _, tail = text.partition(marker)
    main = dedupe_body_sents(head.rstrip())
    if before < TARGET_LO:
        if n in THIRD_TOPUP:
            main = dedupe_body_sents(main + "\n" + THIRD_TOPUP[n].strip())
        elif n in EXTRA_TOPUP:
            main = dedupe_body_sents(main + "\n" + EXTRA_TOPUP[n].strip())
        elif n in TOPUP:
            main = dedupe_body_sents(main + "\n" + TOPUP[n].strip())
    main = dedupe_body_sents(main)
    new_text = main + marker + tail
    path.write_text(new_text, encoding="utf-8")

    after = body_chars(new_text)
    body2, _ = extract_body_and_footer(new_text)
    sents = [s.strip() for s in re.split(r"[。！？\n]", body2) if len(s.strip()) >= 4]
    seen, dup = set(), 0
    for s in sents:
        if s in seen:
            dup += 1
        seen.add(s)
    ratio = dup / len(sents) if sents else 0.0
    return before, after, ratio


