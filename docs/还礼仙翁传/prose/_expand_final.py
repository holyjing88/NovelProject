def dedupe_body(body):
    paras = [p.strip() for p in body.split('\n\n') if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r'\s+', '', p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return '\n\n'.join(out)

def build_chapters():
    chapters = {}
    chapters.update(load_chapters('_expand_batch2.py'))
    chapters.update(load_chapters('_expand_batch2b.py'))
    out = {}
    for fn, content in chapters.items():
        parts = [EXTRAS.get(fn, ''), MORE.get(fn, ''), PAD.get(fn, ''), FINAL.get(fn, ''), TOPUP.get(fn, '')]
        extra = '\n\n'.join(p.strip() for p in parts if p.strip())
        merged = merge_extra(content, extra)
        for block in (FILLER, FILLER2):
            if count_body(merged) < TARGET and fn in block:
                merged = merge_extra(merged, block[fn])
        title, body, footer = parse_ch(merged)
        body = dedupe_body(body)
        merged = make_ch(title, body, footer)
        out[fn] = merged
    return out

if __name__ == '__main__':
    CHAPTERS = build_chapters()
