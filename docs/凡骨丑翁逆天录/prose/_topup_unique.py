def cjk(t):
    return len(re.findall(r'[\u4e00-\u9fff\u3400-\u4dbf]', t))

def fix_and_topup(path, extra):
    with open(path, encoding='utf-8') as f:
        text = f.read()
    # fix broken structure: body ends with --- before 章末
    if re.search(r'\n---\n\n章末', text) is None and '章末' in text:
        text = text.replace('\n---\n\n章末', '\n\n---\n\n章末')
        if '---' not in text.split('章末')[0][-20:]:
            idx = text.find('章末')
            text = text[:idx].rstrip() + '\n\n---\n\n' + text[idx:]
    parts = text.split('---')
    title = parts[0].split('\n', 1)[0]
    body = '\n'.join(parts[0].split('\n')[1:]).strip()
    footer = '---'.join(parts[1:]).strip()
    body = body.replace(BIG, '')
    body = re.sub(r'更鼓将尽，天边白一线。白里，廊下鼻下静半刻，粟壳香淡。', '', body)
    body = re.sub(r'\n{3,}', '\n\n', body).strip()
    if extra and extra.strip() not in body:
        body = body + '\n\n' + extra.strip()
    with open(path, 'w', encoding='utf-8') as f:
        f.write(f"{title}\n\n{body}\n\n---\n\n{footer}\n")
    return cjk(body)

