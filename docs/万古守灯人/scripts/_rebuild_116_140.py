def strip_generic(text):
    """Remove generic padding paragraphs."""
    paras = [p.strip() for p in re.split(r"\n\s*\n", text) if p.strip()]
    out = []
    for p in paras:
        if any(m in p for m in GENERIC_MARKERS):
            continue
        if p.startswith("---"):
            continue
        out.append(p)
    return "\n\n".join(out)


def dedupe_body(text):
    paras = [p.strip() for p in re.split(r"\n\s*\n", text) if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r"\s+", "", p)[:100]
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


