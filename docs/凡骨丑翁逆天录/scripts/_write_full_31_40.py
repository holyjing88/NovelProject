def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)

def wall_count(body):
    return sum(1 for p in re.split(r"\n\s*\n", body) if len(re.sub(r"\s+", "", p)) > 180)

def check_para(body):
    bad = []
    for i, p in enumerate(re.split(r"\n\s*\n", body)):
        n = len(re.sub(r"\s+", "", p))
        if n > 180:
            bad.append((i, n, p[:60]))
    return bad

