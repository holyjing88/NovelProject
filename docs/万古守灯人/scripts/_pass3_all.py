def replace_chapter(text, start_title, end_title, body):
    s = text.index(start_title)
    e = text.index(end_title, s + len(start_title))
    return text[:s] + body.rstrip() + "\n\n---\n\n" + text[e:]


def hanzi_count(block):
    return len(re.sub(r"\s", "", block)) // 2


def dedupe_consecutive_lines(text):
    lines = text.split("\n")
    out = []
    prev = None
    for line in lines:
        s = line.strip()
        if s and s == prev:
            continue
        out.append(line)
        if s:
            prev = s
    return "\n".join(out)


def dedupe_paragraphs(body):
    paras = re.split(r"\n\s*\n", body.strip())
    seen_keys = []
    out = []
    for p in paras:
        p = p.strip()
        if not p:
            continue
        key = re.sub(r"\s+", "", p)
        if len(key) < 28:
            out.append(p)
            continue
        dup = False
        for sk in seen_keys:
            if key == sk:
                dup = True
                break
            if len(key) > 50 and len(sk) > 50:
                if key in sk or sk in key:
                    shorter, longer = (key, sk) if len(key) < len(sk) else (sk, key)
                    if len(shorter) / len(longer) > 0.55:
                        dup = True
                        break
        if not dup:
            seen_keys.append(key)
            out.append(p)
    return "\n\n".join(out) + ("\n" if out else "")


def dedupe_vol03_from_ch118(text):
    marker = "### 第一百一十八章"
    idx = text.find(marker)
    if idx < 0:
        return text
    head, rest = text[:idx], text[idx:]
    parts = re.split(r"(?=^### 第)", rest, flags=re.M)
    cleaned = []
    for part in parts:
        if not part.strip():
            continue
        m = re.match(r"^(### 第[^\n]+\n)", part)
        if not m:
            cleaned.append(part)
            continue
        title = m.group(1)
        body = part[len(title) :]
        body = dedupe_paragraphs(body)
        body = dedupe_consecutive_lines(body)
        cleaned.append(title + body)
    return head + "".join(cleaned)


