CH65 = """第六层，无火。

顾迟年迈入此层之前，已在塔中走过五关——镜海破讥，火狱踏焰，雾障认路，骨墙不取，影牢只投路。每一关都耗油、耗记忆、耗人间烟火，到得第六层门开时，明魂丹已空，袖中守岁灯只剩豆大一点的温，像迟暮之约里最后一口气。

塔规在此刻显形——**灯道皆废，唯人心**。寻常修士到此，油尽即退；他六十一，杂役，示二阶于外，藏三阶于内，四阶未至，本不该站在这里。

可他站在这里。

漆黑如夜，灯道皆废，唯人心。顾迟年跪地，守岁灯几乎熄灭，豆火颤，风过即灭。

塔外，万人仰首，先见第六层灯灭，继而见塔身一暗，像有人把云岚宗后山的星都吹熄了。

「完了？」有人低声。

「杂役老头撑不过第六层。」有人冷笑，「六十一了，还逞能。」

陆承安立主持台，四阶灯盏在额前微亮，声传全场：「塔规如此，他若死在里面，也算咎由自取。」

霍照临抱臂不语，五阶灯影在背，却第一次微微前倾，像少年看长辈走险路，不能伸手，只能看。

姜小满合十，唇瓣发白，只念：「灯还亮……灯还亮……」

铁柱扛锤，青筋暴起，却被执法弟子拦住：「闯塔者，死罪。」

「俺迟年哥还在里头！」铁柱吼，声裂，「你们这些狗东西——」

姜小满拽他袖：「顾爷爷说等。」

铁柱红眼，锤落半寸，砸碎地砖，像把怒气压进土里。

塔内，顾迟年听不见外议。黑暗里，先闻人声——

「顾老头，回来吧。」是孙福，声发颤，像腿又软了。

「迟年哥，俺替你死！」是铁柱，声发裂，像锤落空了。

「顾爷爷……」姜小满泣声极轻，像怕惊灭豆火。

「迟年，」极淡一声，像沈青禾在青萝，「灯还亮着。」

「价已付，」极冷一声，像裴无妄在幽灯集，「勿悔。」

「三阶勉强，」一声，像霍照临在廊下，「四阶不可取常。」

「死在里面，」一声，像陆承安在殿内，「正好结案。」

诸声交织，像塔在问：你为谁续灯？你为谁取路？你为谁付命？

顾迟年不辩，只问己：「若灯灭，还剩什么？」

脑中闪过一生：落第、誊账、六十大寿、守岁灯、迟暮之约……

答：「还剩帮人。」

答完，他不再调息，不再数油，只把三十年的账册在心头一页页翻过——帮孙福母，耗油半滴；救铁柱，失一段记忆；护青禾，付五滴油与镇口初点灯一念；还周小六，燃最后一缕人间烟火；破镜海，踏火狱，穿雾障，过骨墙，投影路，教十二人自救。

账算清，手不抖。

塔规要人心，他便给人心——不是给塔，是给那些该活的人，给迟暮之约，给万灯大会前那一盏不能灭的灯。

他以指划胸，心血一滴，落入灯芯——非油，是命。

灯再亮，四阶灯盏虚影在顶浮现！

塔外，万人倒吸凉气！

「四阶？！」

「杂役老头四阶灯盏？！」

「他示二阶……这……」

陆承安面色大变，四阶灯盏在额前乱跳，像压不住场。他令心腹动塔基，欲在顾迟年踏出第七层前毁塔——名可正，杀可隐。

霍照临五阶灯影一照，冷声：「谁动塔基，我杀谁。」

心腹退，陆承安面色更青，像吞了断脉草。

云照长老眸光深：「以命续灯，不可取常。」

姜小满合十，泪落：「灯还亮……灯还亮……」

霍照临五阶灯影在背，第一次露出敬色，却仍抱臂不语——约还在，塔还在，恩是恩，约是约。

顾迟年不知外议，扶墙起身，七窍渗血，仍立。血顺灰袍滴落，像账册上的朱批，一笔一笔，都是付出去的命。

第七层门开，威压更重，千年古灯气息扑面，灯芯枯槁，千年将尽。他尚在四阶虚影，未稳，众人以为必止——四阶虚影触门槛，非稳四阶，不能取芯，这是塔规，也是常识。

他却迈步——明魂丹尽，烟火尽，心血已付，只剩一念：

「塔取芯，人取路。」

塔内，顾迟年踏向第七层，守岁灯金红微跳，与塔顶残灯呼应，像一老一少两盏灯在暗里对望。

**灯箓三转**，古篆在芯里闪，无人得见——那是迟暮之约深处的东西，是青萝镇口长明与守岁灯之间的线，线头在他，线尾在第七层门后。

他低念：「急什么，灯还亮着呢。」

黑暗吞没背影——第七层，千年灯芯，在等他炼。

塔外风雨欲来，铁柱要闯，被姜小满拽：「顾爷爷说等。」

孙福带十二杂役齐念：「灯还亮着呢。」

众念如豆，汇成一层薄光，塔外不知能传几层，塔内顾迟年却似感得一丝暖——不是油，是人念。

万人屏息，等下一声钟鸣——那声钟，将决花甲杂役，能否把迟暮之约，走到霍照临五阶灯影之前。

他入第七层黑暗，不辨上下，只辨心——心若还在，灯便还在；灯若还在，约便还在。

四阶虚影未稳，只是触门槛；炼灯取芯，在门后。钟鸣未响，灯芯未落，破塔未完——第六十六章，在门后等着。

顾迟年六十一，杂役，示二阶于外，藏三阶于内，四阶虚影初触，嗅觉已失，油将尽，迟暮之约七百一十余日——够走，不够狂。

第七层门内，先无光，后无声，像一口吞人的井。

顾迟年以心血续灯，四阶虚影在顶，未稳，却够开门——开门不够取芯，取芯在更深处。他知陆承安要他在此死，霍照临要他在此活，云岚宗要他在此证塔规：灯试人心，不试杀心。

他扶墙，血滴在古砖上，砖不吸，像塔在等更重的价。

塔外，陆承安对心腹低声：「第七层若成，便说他盗芯。」心腹颤：「塔规……」陆冷笑：「塔规由胜者写。」

霍照临五阶灯影一照，心腹退三步，像被火烫。

姜小满合十，孙福念诀，铁柱扛锤，三人各守一处，像杂役堂搬上了塔基——顾迟年不在时，杂役堂也要亮。

沈青禾在青萝镇，似觉远方一震，低语：「灯还亮着。」

她不知塔中细节，只知若顾迟年灯灭，青萝镇口那盏长明也会少一分暖——她守铺，便是守这分暖。

顾迟年迈步，黑暗吞没背影。

守岁灯金红微跳，与塔顶残灯呼应，**灯箓三转**古篆在芯里闪，无人得见。

炼灯取芯，尚未开始；破塔出塔，万灯大会，迟暮之约，皆在第六十六章之后。

钟鸣未响，灯芯未落——下一章，在门后等着。

他低念最后一句：「急什么，灯还亮着呢。」"""
