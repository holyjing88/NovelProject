for n, text in EXTRA.items():
    matches = glob.glob(os.path.join(PROSE, f"ch{n:03d}-*.md"))
    fpath = matches[0]
    raw = open(fpath, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    if text not in body:
        body = body.replace("\n---\n\n章末", f"\n\n{text}\n\n---\n\n章末", 1)
        open(fpath, "w", encoding="utf-8").write(body + "\n\n" + footer)
    h = hz(body)
    status = "OK" if h >= TARGET_LO else "SHORT"
    print(f"ch{n:03d}: {h} {status}")
