    for old, new in TITLE_FIXES:
        text = text.replace(old, new)

    for block in DEDUP_BLOCKS:
        text = text.replace(block, "", 1)

    for anchor, insert in INSERTS:
        if anchor not in text:
            print("WARN anchor missing:", anchor[:40])
            continue
        text = text.replace(anchor, insert, 1)

    for anchor, insert in PASS2_INSERTS:
        if anchor not in text:
            print("WARN pass2 missing:", anchor[:40])
            continue
        text = text.replace(anchor, insert, 1)

    VOL1.write_text(text, encoding="utf-8")
