def pick_canonical(ch: int, files: list[str]) -> str:
    if ch in CANONICAL:
        want = CANONICAL[ch]
        for f in files:
            if os.path.basename(f) == want:
                return f
    return sorted(files)[0]


def remove_dup_files():
    for ch in range(69, 130):
        pattern = os.path.join(PROSE, f"ch{ch:03d}-*.md")
        files = glob.glob(pattern)
        if len(files) <= 1:
            continue
        keep = pick_canonical(ch, files)
        for f in files:
            if f != keep:
                os.remove(f)
                print(f"Removed dup ch{ch:03d}: {os.path.basename(f)}")


def validate():
    missing, short, ok, high_dup = [], [], [], []
    bodies = {}
    for n in range(69, 130):
        pattern = os.path.join(PROSE, f"ch{n:03d}-*.md")
        files = glob.glob(pattern)
        if not files:
            missing.append(n)
            continue
        if len(files) > 1:
            print(f"STILL DUP ch{n}: {files}")
        path = pick_canonical(n, files)
        with open(path, encoding="utf-8") as f:
            text = f.read()
        bc = body_chars(text)
        body, _ = extract_body_and_footer(text)
        bodies[n] = norm(body)
        if bc < TARGET_LO or bc > TARGET_HI:
            short.append((n, bc, os.path.basename(path)))
        else:
            ok.append((n, bc))

    def ngram_sim(a, b, ng=20):
        if len(a) < ng or len(b) < ng:
            return 0.0
        sa = {a[i : i + ng] for i in range(len(a) - ng + 1)}
        sb = {b[i : i + ng] for i in range(len(b) - ng + 1)}
        return len(sa & sb) / len(sa) if sa else 0.0

    keys = sorted(bodies)
    for i, a in enumerate(keys):
        for b in keys[i + 1 :]:
            s = ngram_sim(bodies[a], bodies[b])
            if s >= 0.02:
                high_dup.append((a, b, s))

    return missing, short, ok, high_dup


