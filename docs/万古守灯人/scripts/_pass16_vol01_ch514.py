def audit():
    text = VOL1.read_text(encoding="utf-8")
    order = ["五", "六", "七", "八", "九", "十", "十一", "十二", "十三", "十四"]
    for m in re.finditer(r"### (第.{1,8}章[^\n]*)\n(.*?)(?=\n---\n|\n### 第|\Z)", text, re.S):
        num = re.search(r"第(.{1,8})章", m.group(1)).group(1)
        if num in order:
            c = len(re.findall(r"[\u4e00-\u9fff]", m.group(2)))
            st = "OK" if c >= 2500 else f"SHORT {2500 - c}"
            print(num, c, st)


