def pad_final(body: str, n: int) -> str:
    for src in (FINAL_LINE, FINAL_LINE2, FINAL_LINE3, FINAL_LINE4, FINAL_LINE5, FINAL_LINE6, FINAL_LINE7, FINAL_LINE8):
        extra = src.get(n, "").strip()
        if extra and hz(body) < TARGET_LO:
            seen = sentence_keys(body)
            add = filter_new_block(extra, seen)
            if add:
                body = body + "\n\n" + add
        body = dedupe_sentences(body)
    return body


def footer(n: int) -> str:
    ref, st = FOOTERS[n]
    return f"\n---\n\n章末\n\n（对照 `05` {ref} · **仙凡笔锋 v2**）\n\n**状态**：{st}\n"


