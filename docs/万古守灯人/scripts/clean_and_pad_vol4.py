def cn_len(s):
    return len(re.sub(r"\s", "", s))


def parse_num(title):
    for k, v in CN_NUM.items():
        if k in title:
            return v
    return 0


def dedupe_paragraphs(body):
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r"\s", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


def main():
    text = PATH.read_text(encoding="utf-8")
    for pat in REMOVE:
        text = re.sub(pat, "", text)
    text = re.sub(r"\n{3,}", "\n\n", text)

    parts = re.split(r"(?=### 第)", text)
    chapters = [p for p in parts[1:] if p.startswith("### 第")]
    out = [HEADER]
    stats = []

    for ch in chapters:
        m = re.match(r"### (第.+?章 .+?)\n\n", ch)
        title = m.group(1)
        num = parse_num(title)
        body = re.sub(r"^###[^\n]+\n+", "", ch)
        body = re.sub(r"\n---\s*$", "", body).strip()
        body = re.sub(r"\n\*\*第四卷完\*\*\s*$", "", body).strip()
        body = dedupe_paragraphs(body)
        pad = PADS.get(num, "")
        if pad and pad not in body:
            body = body + "\n\n" + pad
        # trim summary duplicates at end (short lines repeating plot)
        while cn_len(body) > 1800:
            paras = body.split("\n\n")
            body = "\n\n".join(paras[:-1])
        while cn_len(body) < 1200 and num in PADS:
            body += "\n\n" + PADS[num][:200]
        if num == 190:
            body = re.sub(r"\n\*\*第四卷完\*\*\s*$", "", body).strip()
            body += "\n\n**第四卷完**"
        stats.append((title, cn_len(body)))
        out.append(f"### {title}\n\n{body}\n\n---\n\n")

    PATH.write_text("".join(out).rstrip() + "\n", encoding="utf-8")
    ok = sum(1 for _, l in stats if 1200 <= l <= 1800)
    print(f"OK {ok}/{len(stats)}")
    for t, l in stats:
        print(("OK" if 1200 <= l <= 1800 else "!!"), l, t[:20])

if __name__ == "__main__":
    main()
