    m = re.search(pat, text, re.DOTALL)
    if not m:
        print('missing', title)
        continue
    body = m.group(2).strip()
    n0 = count(body)
    if n0 >= 3500:
        print(title, n0, 'ok')
        continue
    new_body = body + '\n\n' + extra.strip()
    n1 = count(new_body)
    text = text[:m.start(2)] + '\n' + new_body + '\n' + text[m.end(2):]
    print(title, n0, '->', n1)

