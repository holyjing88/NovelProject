for n, t in PAD.items():
    p = glob.glob(os.path.join(PROSE, f"ch{n:03d}-*.md"))[0]
    raw = open(p, encoding="utf-8").read()
    body, footer = extract_body_and_footer(raw)
    if t[:20] not in body:
        body = body + "\n\n" + t
        open(p, "w", encoding="utf-8").write(body + "\n\n" + footer)
    h, hd = hz(body), dup(body)
    status = "OK" if TARGET_LO <= h <= TARGET_HI and hd < 0.02 else "FIX"
    print(f"ch{n:03d}: {h} dup={hd:.3f} {status}")

