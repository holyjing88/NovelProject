def get_thicken_block(name: str) -> str:
    parts = [THICKEN[name].strip()]
    if name in THICKEN_EXT:
        parts.append(THICKEN_EXT[name].strip())
    return "\n\n".join(parts)


def block_already_present(body: str, block: str) -> bool:
    first_para = block.strip().split("\n\n")[0]
    key = _norm(first_para)[:40]
    if len(key) >= 20 and key in _norm(body):
        return True
    key = _norm(block)[:60]
    return len(key) >= 40 and key in _norm(body)


def split_body_footer(text: str) -> tuple[str, str]:
    body, footer = extract_body_and_footer(text)
    if body and not body.endswith("\n"):
        body += "\n"
    return body, footer


def _norm(s: str) -> str:
    return re.sub(r"\s+", "", s)


def remove_duplicate_paragraphs(body: str) -> str:
    """Remove paragraphs whose 80+ char normalized substring already appeared."""
    paragraphs = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
    if len(paragraphs) < 2:
        return body
    seen_subs: set[str] = set()
    result: list[str] = []
    for p in paragraphs:
        p_norm = _norm(p)
        is_dupe = False
        for i in range(max(0, len(p_norm) - 79)):
            sub = p_norm[i : i + 80]
            if sub in seen_subs:
                is_dupe = True
                break
        if not is_dupe:
            result.append(p)
            for i in range(max(0, len(p_norm) - 79)):
                seen_subs.add(p_norm[i : i + 80])
    # Remove shorter paragraphs contained in an earlier longer one
    cleaned: list[str] = []
    for p in result:
        p_norm = _norm(p)
        skip = False
        for j, prev in enumerate(cleaned):
            prev_norm = _norm(prev)
            if len(p_norm) >= 80 and p_norm in prev_norm:
                skip = True
                break
            if len(prev_norm) >= 80 and prev_norm in p_norm:
                cleaned[j] = p
                skip = True
                break
        if not skip:
            cleaned.append(p)
    return "\n\n".join(cleaned) + ("\n" if cleaned else "")


def dedup_body(body: str) -> str:
    """If last 200 chars repeat an 80+ char substring from earlier, drop last paragraph."""
    changed = True
    while changed:
        changed = False
        paragraphs = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
        if len(paragraphs) < 2:
            break
        last_para = paragraphs[-1]
        check_zone = last_para[-200:] if len(last_para) >= 200 else last_para
        earlier = _norm("\n\n".join(paragraphs[:-1]))
        for length in range(min(len(check_zone), 120), 79, -1):
            for start in range(len(check_zone) - length + 1):
                sub = _norm(check_zone[start : start + length])
                if len(sub) >= 80 and sub in earlier:
                    paragraphs = paragraphs[:-1]
                    body = "\n\n".join(paragraphs) + "\n"
                    changed = True
                    break
            if changed:
                break
    return body


def fix_ch010_duplicates(body: str) -> str:
    """Remove known duplicate blocks in ch010."""
    body = remove_duplicate_paragraphs(body)
    marker = "药渣之心的余波，还在外门盘桓"
    if body.count(marker) > 1:
        idx = body.find(marker)
        before = body[:idx]
        rest = body[idx + len(marker) :]
        chunks = [c for c in rest.split(marker) if c.strip()]
        if chunks:
            best = max(chunks, key=lambda c: len(re.findall(r"[\u4e00-\u9fff]", c)))
            body = before + marker + best
    # Keep tail section if removed with duplicate boost
    if "更深一层，药渣之心还在弟子的舌根上滚" not in body:
        tail = get_tail_block("ch010-药渣之心.md")
        if tail.strip() and tail not in body:
            body = body.rstrip() + "\n\n" + tail.strip() + "\n"
    known_dup_starts = (
        "廊外，弟子窃语未歇，这一回却多一句",
        "苏念慈握瓶，低声：「回赠……不是还债」",
        "赵玄机在执法堂抄门规，抄到「私赠丹药」一条",
    )
    paragraphs = [p.strip() for p in re.split(r"\n\n+", body.strip()) if p.strip()]
    seen_keys: set[str] = set()
    cleaned: list[str] = []
    for p in paragraphs:
        skip = False
        for key in known_dup_starts:
            if key in p:
                if key in seen_keys:
                    skip = True
                    break
                seen_keys.add(key)
        if not skip:
            cleaned.append(p)
    return "\n\n".join(cleaned) + ("\n" if cleaned else "")


def insert_before_footer(body: str, block: str, footer: str) -> str:
    block = block.strip()
    if not block:
        return body.rstrip() + "\n" + footer
    return body.rstrip() + "\n\n" + block + "\n" + footer


def process_file(path: Path, dry_run: bool = False) -> dict:
    name = path.name
    text = path.read_text(encoding="utf-8")
    before = body_chars(text)
    body, footer = split_body_footer(text)

    if name == "ch010-药渣之心.md":
        body = fix_ch010_duplicates(body)
    body = dedup_body(body)
    body = remove_duplicate_paragraphs(body)

    current = body_chars(body)
    thickened = False
    if name not in SKIP_THICKEN and name in THICKEN:
        getters = (get_thicken_block,)  # 禁用 get_boost_block，防 rumor 模板回流
        for _ in range(4):
            if body_chars(body) >= LO:
                break
            added = False
            for block_getter in getters:
                if body_chars(body) >= LO:
                    break
                block = block_getter(name)
                if not block.strip() or block_already_present(body, block):
                    continue
                paras = [p.strip() for p in re.split(r"\n\n+", block.strip()) if p.strip()]
                good = "\n\n".join(
                    p for p in paras
                    if not any(m in p for m in (
                        "的余波，还在外门盘桓",
                        "哼里空一线——线若拉满",
                        "踮脚像钩——钩向下章",
                        "莫长春望天，星稀",
                    ))
                )
                if good.strip():
                    body = body.rstrip() + "\n\n" + good.strip() + "\n"
                    thickened = True
                    added = True
            if not added:
                break
        body = dedup_body(body)
        body = remove_duplicate_paragraphs(body)

    new_text = body.rstrip() + "\n" + footer
    after = body_chars(new_text)

    if not dry_run and new_text != text:
        path.write_text(new_text, encoding="utf-8")

    return {
        "name": name,
        "before": before,
        "after": after,
        "thickened": thickened,
        "changed": new_text != text,
    }


def main() -> None:
    rows: list[dict] = []
    for p in sorted(PROSE.glob("ch*.md")):
        if p.name not in CANONICAL:
            continue
        rows.append(process_file(p))

    thickened = [r for r in rows if r["thickened"]]
    in_range = sum(1 for r in rows if LO <= r["after"] <= HI)
    below = [r for r in rows if r["after"] < LO]
    over = [r for r in rows if r["after"] > HI]

    print(f"Processed {len(rows)} chapters (ch001-061)")
    print(f"Thickened: {len(thickened)}")
    print(f"TARGET={TARGET}, range={LO}-{HI}")
    print(f"in_range_{LO}-{HI}: {in_range}/{len(rows)}")
    print(f"below{LO}: {len(below)}")
    for r in sorted(below, key=lambda x: x["after"]):
        print(f"  {r['name']}: {r['before']} -> {r['after']}")
    print(f"over{HI}: {len(over)}")
    for r in sorted(over, key=lambda x: -x["after"]):
        print(f"  {r['name']}: {r['before']} -> {r['after']}")
    print("\nThickened chapters:")
    for r in thickened:
        print(f"  {r['name']}: {r['before']} -> {r['after']}")
    print("\nSkipped (already >= LO or not in THICKEN):")
    for r in rows:
        if r["name"] in SKIP_THICKEN:
            print(f"  {r['name']}: {r['after']} (skip list)")
        elif r["before"] >= LO and not r["thickened"]:
            print(f"  {r['name']}: {r['after']} (already >= {LO})")


if __name__ == "__main__":
    main()
