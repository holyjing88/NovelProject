def dedupe_sents(text: str) -> str:
    parts = re.split(r"(?<=[。！？])", text)
    seen: set[str] = set()
    out: list[str] = []
    for p in parts:
        p = p.strip()
        if not p:
            continue
        k = re.sub(r"\s+", "", p)
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return "".join(out)


def dedupe_body_sents(text: str) -> str:
    """Remove duplicate clauses (dup_ratio semantics), keep first."""
    parts = re.split(r"(?<=[。！？\n])", text)
    seen: set[str] = set()
    out: list[str] = []
    for p in parts:
        s = p.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(k) < 4:
            out.append(p)
            continue
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return "".join(out)


