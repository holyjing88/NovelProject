def insert_expand(body: str, block: str) -> str:
    block = block.strip()
    if not block:
        return body
    # 插在「下一份」或末段收束前
    m = re.search(r"\n\n莫长春袖空，一念收塔", body)
    if m:
        return body[: m.start()] + "\n\n" + block + body[m.start() :]
    m = re.search(r"\n\n莫长春念：「下一份", body)
    if m:
        return body[: m.start()] + "\n\n" + block + body[m.start() :]
    m = re.search(r"\n\n莫长春袖空，一念出塔.*\n\n---", body, re.S)
    if m:
        return body[: m.start()] + "\n\n" + block + "\n\n" + body[m.start() :]
    return body.rstrip() + "\n\n" + block


