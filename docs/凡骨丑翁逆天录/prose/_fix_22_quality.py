BANNED = re.compile(r"第\d+章|下一章|这章|读者若问")


def full_chapter(title, body, footer):
    return f"# {title}\n\n{body.strip()}\n\n---\n\n{footer.strip()}\n"


def clean_body(body):
    body = BANNED.sub("", body)
    body = re.sub(r"\n{3,}", "\n\n", body)
    # remove broken quote-only lines
    lines = []
    for line in body.split("\n"):
        s = line.strip()
        if s in ('"', "」", "「", "」", "」韩泥"):
            continue
        lines.append(line)
    return "\n".join(lines).strip()


