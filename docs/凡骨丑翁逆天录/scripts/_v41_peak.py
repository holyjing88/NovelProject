def scrub_meta(text: str, in_footer: bool = False) -> str:
    if not in_footer:
        for pat, rep in META_CH:
            text = re.sub(pat, rep, text)
    text = text.replace("记账", "记在心里")
    text = text.replace("入账", "记在心里")
    return text


def dedupe_paras(text: str) -> str:
    paras = [p.strip() for p in re.split(r"\n\n+", text.strip()) if p.strip()]
    seen: set[str] = set()
    out: list[str] = []
    for p in paras:
        key = re.sub(r"\s+", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(dedupe_sents(p))
    return "\n\n".join(out)


def extract_main(body: str) -> str:
    m = re.search(r"^(.*?)(?=\n\n---\n\n章末)", body, re.S)
    return m.group(1).strip() if m else body.strip()


def rebuild_footer(n: int, old_footer: str) -> str:
    m = re.search(r"（对照.*?）", old_footer)
    note = m.group(0) if m else f"（对照 `05` §{n} · **v41综合10**）"
    if "v41" not in note:
        note = note.rstrip("）") + " · **v41综合10**）"
    return f"\n\n---\n\n章末。\n\n{note}\n\n{status_line(n)}\n"


def process_ch063(text: str) -> str:
    """ch063 去重末段重复。"""
    body, _ = extract_body_and_footer(text)
    body = scrub_meta(body)
    # 删除重复「不追问像少年」段
    dup_block = (
        "韩泥不追问。追问像少年。他只按掌，按实，按到掌烫，烫得踏实。"
    )
    first = body.find(dup_block)
    if first >= 0:
        second = body.find(dup_block, first + len(dup_block))
        if second >= 0:
            body = body[:second] + body[second + len(dup_block) :]
    body = dedupe_paras(body)
    footer = rebuild_footer(63, text)
    return body + footer


def process_ch130(text: str) -> str:
    body, _ = extract_body_and_footer(text)
    body = scrub_meta(body)
    # 删重复刘婆点头段
    old = (
        "刘婆点头，点得像补衣时顶针顶布，一下，又一下。新衣布糙，糙得真，真得像恩。"
    )
    if body.count(old) > 1:
        body = body.replace(old, "", 1)
    # 删重复胸空半寸
    dup = "减得他胸空半寸，空得舒服——舒服不是无债，是债还了一笔，还能还下一笔。"
    if body.count(dup) > 1:
        body = body.replace(dup, "", 1)
    # 删重复袖里棉味
    dup2 = "袖里像还留着刘婆新衣的棉味"
    if body.count(dup2) > 1:
        idx = body.rfind(dup2)
        if idx > 0:
            line_start = body.rfind("\n", 0, idx)
            line_end = body.find("\n", idx)
            if line_end > line_start:
                body = body[:line_start] + body[line_end:]
    body = dedupe_paras(body)
    if hz(body) < TARGET_LO:
        body += (
            "\n\n他望山门帖，帖上言伯钧名，上钱戾衡影。"
            "影在字后，像仇在路后；路后不要紧，刘婆这笔已还，还了，丹才正。"
            "正，是五年淘出来的，不是运气——运气救不了丑骨，手稳才能。"
        )
    footer = rebuild_footer(130, text)
    return body + footer


def process_file(path: str) -> tuple[int, int, float, bool]:
    n = chapter_num(path)
    if n < 50 or (64 <= n <= 129):
        return 0, 0, 0.0, False
    text = open(path, encoding="utf-8").read()
    before = hz(extract_body_and_footer(text)[0])

    if n == 63:
        new_text = process_ch063(text)
    elif n == 130:
        new_text = process_ch130(text)
    else:
        body, _ = extract_body_and_footer(text)
        main = extract_main(body)
        main = scrub_meta(main)
        if n in EXTEND:
            main = main.rstrip() + "\n\n" + EXTEND[n]
        main = dedupe_paras(main)
        new_text = main + rebuild_footer(n, text)

    after = hz(extract_body_and_footer(new_text)[0])
    dup = _dup(extract_body_and_footer(new_text)[0])
    changed = new_text != text
    if changed:
        open(path, "w", encoding="utf-8", newline="\n").write(new_text)
    return before, after, dup, changed


def _dup(t: str) -> float:
    s = [x.strip() for x in SPLIT_DEDUP.split(t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def main() -> None:
    updated = short = high = over = 0
    for path in sorted(glob.glob(os.path.join(PROSE, "ch*.md"))):
        n = chapter_num(path)
        if n < 50 or (64 <= n <= 129):
            continue
        b, a, dup, ch = process_file(path)
        if ch:
            updated += 1
            print(f"ch{n:03d} {b}->{a} dup={dup:.3f}")
        if a < TARGET_LO:
            short += 1
        if a > TARGET_HI:
            over += 1
        if dup >= 0.02:
            high += 1
    print(f"v41: updated={updated} short={short} over={over} dup>=2%={high}")


if __name__ == "__main__":
    main()
