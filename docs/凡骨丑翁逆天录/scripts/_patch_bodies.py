def fill_chapter(body: str, n: int) -> str:
    for line in POOL.get(n, []) + FRESH.get(n, []):
        if hz(body) >= 1550:
            break
        if has_sent(body, line):
            continue
        trial = body.rstrip() + "\n" + line
        if dup_ratio(trial) >= 0.02:
            continue
        body = trial
    return body


