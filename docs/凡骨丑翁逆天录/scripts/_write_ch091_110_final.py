def dup(t):
    s = [x.strip() for x in re.split(r"(?<=[。！？])", t) if len(x.strip()) >= 8]
    if not s:
        return 0.0
    seen, d = set(), 0
    for x in s:
        k = re.sub(r"\s+", "", x)
        if k in seen:
            d += 1
        seen.add(k)
    return d / len(s)


def split_body_footer(raw_body: str):
    """Gen bodies include --- footer; split."""
    m = re.search(r"\n---\n\n章末", raw_body)
    if m:
        return raw_body[: m.start()].strip(), raw_body[m.start() :].strip()
    return raw_body.strip(), ""


def insert_before_hook(body: str, insert: str) -> str:
    insert = insert.strip()
    if not insert:
        return body
    parts = body.rsplit("\n\n", 1)
    if len(parts) == 2:
        return parts[0] + "\n\n" + insert + "\n\n" + parts[1]
    return body + "\n\n" + insert


