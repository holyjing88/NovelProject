def build_chapter(n: int, fname: str, title: str, raw_body: str):
    body, footer = split_body_footer(raw_body)
    if n in PAD:
        body = insert_before_hook(body, PAD[n])
    if n in EXTRA:
        body = insert_before_hook(body, EXTRA[n])
    return f"# {title}\n\n{body.strip()}\n\n{footer.strip()}\n"


def main():
    results = []
    for fname, title, raw_body in gen.CHAPTERS:
        n = int(re.search(r"ch(\d+)", fname).group(1))
        content = build_chapter(n, fname, title, raw_body)
        path = os.path.join(PROSE, fname)
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        b, _ = extract_body_and_footer(content)
        h, d = hz(b), dup(b)
        ok = TARGET_LO <= h <= TARGET_HI and d < 0.02
        results.append((fname, h, round(d, 4), ok))

    print("FILE\tCHARS\tDUP\tOK")
    for r in results:
        print(f"{r[0]}\t{r[1]}\t{r[2]}\t{r[3]}")
    bad = [r for r in results if not r[3]]
    if bad:
        print("NEEDS_FIX", bad)
        sys.exit(1)
    print("ALL_OK", len(results))


if __name__ == "__main__":
    main()
