def dedupe_body(body: str) -> str:
    """Remove duplicate paragraph blocks (exact or near)."""
    paras = [p.strip() for p in re.split(r"\n\n+", body) if p.strip()]
    seen = set()
    out = []
    for p in paras:
        key = re.sub(r"\s+", "", p)
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return "\n\n".join(out)


