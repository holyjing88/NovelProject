def fix_chapter(n):
    files = glob.glob(os.path.join("..", "prose", f"ch{n:03d}-*.md"))
    path = files[0]
    raw = open(path, encoding="utf-8").read()
    m = re.search(r"\n---\n\n章末", raw)
    header = raw[:raw.find("\n\n")+2] if raw.startswith("#") else ""
    body = raw[len(header):m.start()] if m else raw
    footer = raw[m.start():] if m else ""
    # extract title line
    lines = raw.split("\n")
    title = lines[0]
    body_raw = "\n".join(lines[1:m.start()] if m else lines[1:])
    body = dedupe_body(body_raw)
    if n in UNIQUE:
        body = body.rstrip() + "\n" + UNIQUE[n].strip()
        body = dedupe_body(body)
    new_raw = title + "\n\n" + body + "\n" + footer
    open(path, "w", encoding="utf-8").write(new_raw)
    b, _ = extract_body_and_footer(new_raw)
    h, d, w = hz(b), dup(b), wall_count(b)
    ok = TARGET_LO <= h <= TARGET_HI and d < 0.02 and w == 0
    print(f"ch{n:03d} hz={h} dup={d:.3f} WALL={w} {'OK' if ok else 'FAIL'}")
    return ok

if __name__ == "__main__":
    all_ok = True
    for n in range(33, 41):
        if not fix_chapter(n):
            all_ok = False
    print("ALL_OK", all_ok)
