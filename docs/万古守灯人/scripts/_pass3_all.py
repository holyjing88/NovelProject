def measure_chapters(vol_path, keys):
    t = vol_path.read_text(encoding="utf-8")
    out = {}
    for label, start in keys:
        i = t.find(start)
        if i < 0:
            continue
        j = t.find("### 第", i + 5)
        b = t[i:j] if j > 0 else t[i:]
        out[label] = hanzi_count(b)
    return out


