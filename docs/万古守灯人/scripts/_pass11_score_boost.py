def replace_between(text, start, end, body):
    s = text.index(start)
    e = text.index(end, s + len(start)) if end else len(text)
    return text[:s] + body.rstrip() + "\n\n" + text[e:]


def dedupe_paragraphs(body):
    paras = re.split(r"\n\s*\n", body.strip())
    seen, out = [], []
    for p in paras:
        p = p.strip()
        if not p:
            continue
        key = re.sub(r"\s+", "", p)
        if len(key) < 28:
            out.append(p)
            continue
        dup = False
        for sk in seen:
            if key == sk:
                dup = True
                break
            if len(key) > 45 and len(sk) > 45:
                a, b = (key, sk) if len(key) < len(sk) else (sk, key)
                if a in b and len(a) / len(b) > 0.62:
                    dup = True
                    break
        if not dup:
            seen.append(key)
            out.append(p)
    return "\n\n".join(out) + ("\n" if out else "")


