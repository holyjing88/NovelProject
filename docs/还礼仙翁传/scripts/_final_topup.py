def main() -> None:
    for name, block in TOPUP.items():
        p = PROSE / name
        text = p.read_text(encoding="utf-8")
        body, footer = extract_body_and_footer(text)
        b = block.strip()
        if b not in body:
            m = re.search(r"\n\n莫长春袖空，一念收塔", body)
            if m:
                body = body[: m.start()] + "\n\n" + b + body[m.start() :]
            else:
                body = body.rstrip() + "\n\n" + b
        if not footer:
            footer = f"---\n\n*（上架连载稿 · {p.stem.split('-', 1)[1]}）*\n"
        p.write_text(body.rstrip() + "\n\n" + footer, encoding="utf-8")
        c = hz(body)
        st = "OK" if LO <= c <= 2600 else ("LOW" if c < LO else "HIGH")
        print(f"{name}: {c} ({st})")


if __name__ == "__main__":
    main()
