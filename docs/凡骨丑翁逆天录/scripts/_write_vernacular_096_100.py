def build(n: int) -> str:
    c = CH[n]
    body = c["body"].strip() + "\n"
    return f"{body}\n---\n\n章末\n\n{c['footer']}\n\n{c['status']}\n"


def qa_one(n: int, text: str):
    body, footer = extract_body_and_footer(text)
    h = hz(body)
    dr, dups = dup_rate(body)
    walls = wall_paras(body)
    issues = []
    if not (2000 <= h <= 2500):
        issues.append(f"LEN{h}")
    if dr > 0:
        issues.append(f"dup={dr:.3f}")
        if dups:
            issues.append("DUP:" + re.sub(r"\s+", "", dups[0])[:50])
    if walls:
        issues.append(f"WALL{walls[0][0]}")
    if "通俗笔锋" not in footer:
        issues.append("FOOT")
    q = body.count("「")
    if q < 12:
        issues.append(f"Q{q}")
    hk = [k for k in HOOK_KEY if k in hook_tail(body)]
    if not hk:
        issues.append("NOHOOK")
    for ban in ("恩辱分列", "分列不混", "怒值满", "仙凡笔锋", "药峰分拣", "下一章", "读者若问"):
        if ban in body:
            issues.append("BAN:" + ban)
    return h, dr, q, walls, hk, issues, dups


