def process(ch):
    files = glob.glob(os.path.join(PROSE, f"ch{ch:03d}-*.md"))
    if not files:
        return -1
    path = sorted(files)[0]
    text = open(path, encoding="utf-8").read()
    body, footer = extract_body_and_footer(text)
    lines = body.split("\n")
    title = lines[0] if lines[0].startswith("#") else ""
    body_text = "\n".join(lines[1:]).strip() if title else body
    body_text = dedupe_paragraphs(strip_templates(body_text))
    blocks = list(EXPAND.get(ch, [])) + list(TOPUP.get(ch, []))
    seq = 0
    while body_chars(body_text) < TARGET_LO and seq < len(blocks):
        paras = body_text.split("\n\n")
        if blocks[seq] not in paras:
            paras.insert(min(len(paras), 1 + seq), blocks[seq])
            body_text = dedupe_paragraphs("\n\n".join(paras))
        seq += 1
    new_full = (title + "\n\n" + body_text).strip() if title else body_text
    if footer:
        new_full += "\n\n" + footer.lstrip("\n")
    if body_chars(new_full) > TARGET_HI:
        paras = body_text.split("\n\n")
        while body_chars("\n\n".join(paras)) > TARGET_HI and len(paras) > 6:
            paras.pop(-2)
        body_text = "\n\n".join(paras)
        new_full = (title + "\n\n" + body_text).strip() if title else body_text
        if footer:
            new_full += "\n\n" + footer.lstrip("\n")
    open(path, "w", encoding="utf-8").write(new_full + "\n")
    return body_chars(new_full)


