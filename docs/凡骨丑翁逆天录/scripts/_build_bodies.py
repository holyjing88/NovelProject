def hz(s: str) -> int:
    return len(re.findall(r"[\u4e00-\u9fff]", s))


def dup_ratio(t: str) -> float:
    sents = [s.strip() for s in re.split(r"[。！？\n]", t) if len(s.strip()) >= 4]
    if not sents:
        return 0.0
    seen, dup = set(), 0
    for s in sents:
        if s in seen:
            dup += 1
        seen.add(s)
    return dup / len(sents)


def dedupe_body(text: str) -> str:
    parts = re.split(r"(?<=[。！？\n])", text)
    seen: set[str] = set()
    out: list[str] = []
    for p in parts:
        s = p.strip()
        if not s:
            continue
        k = re.sub(r"\s+", "", s)
        if len(k) < 4:
            out.append(p)
            continue
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return "".join(out)


