def insert_block(body: str, block: str) -> str:
    block = block.strip()
    if block in body:
        return body
    if TOPUP_M in body:
        body = re.sub(
            rf"({re.escape(TOPUP_M)}\n\n)(.*?)(?=\n\n{re.escape(END_M)}|\n\n\*\*状态\*\*)",
            lambda m: m.group(1) + m.group(2).rstrip() + "\n\n" + block,
            body,
            count=1,
            flags=re.S,
        )
        return body
    if END_M in body:
        return body.replace(f"\n\n{END_M}", f"\n\n{TOPUP_M}\n\n{block}\n\n{END_M}", 1)
    return body + f"\n\n{TOPUP_M}\n\n{block}"


def main() -> None:
    short = []
    for path in sorted(glob.glob(os.path.join(PROSE, "ch*.md"))):
        n = int(re.search(r"ch(\d+)", path).group(1))
        if n > 49:
            continue
        text = open(path, encoding="utf-8").read()
        body, footer = extract_body_and_footer(text)
        if hz(body) >= TARGET_LO:
            continue
        blocks = list(EXTRA.get(n, []))
        gi = 0
        while hz(body) < TARGET_LO and (blocks or gi < len(GENERIC)):
            if blocks:
                body = insert_block(body, blocks.pop(0))
            elif gi < len(GENERIC):
                body = insert_block(body, GENERIC[gi])
                gi += 1
            else:
                break
        new = body + ("\n\n" + footer if footer else "")
        open(path, "w", encoding="utf-8", newline="\n").write(new)
        c = body_chars(new)
        print(f"ch{n:03d} -> {c}")
        if c < TARGET_LO:
            short.append((n, c))
    print("still short:", short)


if __name__ == "__main__":
    main()
